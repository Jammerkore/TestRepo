CREATE VIEW [dbo].[VW_DETAIL_ALLOCATION]

AS
 
SELECT HDR_RID,
	ST_RID,
	UNITS_ALLOCATED,
	UNITS_ALLOCATED_BY_AUTO,
	UNITS_ALLOCATED_BY_RULE,
	CHOSEN_RULE_TYPE_ID,
	CHOSEN_RULE_LAYER_ID,
	NEED_DAY,
	UNIT_NEED_BEFORE,
	PERCENT_NEED_BEFORE,
	MINIMUM,
	MAXIMUM,
	PRIMARY_MAX,
	ALLOC_STORE_DET_AUDIT_FLAGS,
	CHOSEN_RULE_UNITS,
	ITEM_UNITS_ALLOCATED
FROM DETAIL_ALLOCATION

GO
