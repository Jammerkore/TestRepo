CREATE VIEW [dbo].[VW_GET_STORE_GROUP_CHAR_JOIN]
( ST_RID, ST_ID, STORE_NAME, STORE_DESC, ACTIVE_IND, CITY, 
STATE, SELLING_SQ_FT, SELLING_OPEN_DATE, SELLING_CLOSE_DATE, 
STOCK_OPEN_DATE, STOCK_CLOSE_DATE, LEAD_TIME, SHIP_ON_MONDAY, 
SHIP_ON_TUESDAY, SHIP_ON_WEDNESDAY, SHIP_ON_THURSDAY, SHIP_ON_FRIDAY, 
SHIP_ON_SATURDAY, SHIP_ON_SUNDAY, NUMBER_VALUE, TEXT_VALUE, 
DATE_VALUE, DOLLAR_VALUE, SC_RID, SCG_RID, 
SCG_LIST_IND ) AS SELECT TOP 100 PERCENT STORES.ST_RID, ST_ID, STORE_NAME, STORE_DESC, ACTIVE_IND, CITY, STATE, COALESCE(SELLING_SQ_FT,0) SELLING_SQ_FT, SELLING_OPEN_DATE, SELLING_CLOSE_DATE, STOCK_OPEN_DATE, STOCK_CLOSE_DATE, COALESCE(LEAD_TIME,0) LEAD_TIME,  
SHIP_ON_MONDAY, SHIP_ON_TUESDAY, SHIP_ON_WEDNESDAY, SHIP_ON_THURSDAY, SHIP_ON_FRIDAY, SHIP_ON_SATURDAY, SHIP_ON_SUNDAY, 
NUMBER_VALUE, TEXT_VALUE, DATE_VALUE, DOLLAR_VALUE, SCJ.SC_RID, SCG.SCG_RID, SCG_LIST_IND  
FROM STORES LEFT OUTER JOIN STORE_CHAR_JOIN SCJ ON STORES.ST_RID = SCJ.ST_RID 
LEFT OUTER JOIN STORE_CHAR SC ON SCJ.SC_RID = SC.SC_RID 
LEFT OUTER JOIN STORE_CHAR_GROUP SCG ON SC.SCG_RID = SCG.SCG_RID 
where STORES.ACTIVE_IND = '1' 
ORDER BY STORES.ST_RID

GO
