CREATE PROCEDURE [dbo].[MID_USER_OPTIONS_READ]
	@USER_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		COALESCE(MY_HIERARCHY,' ') MY_HIERARCHY, 
		COALESCE(MY_HIERARCHY_COLOR,' ') MY_HIERARCHY_COLOR, 
		MY_WORKFLOWMETHODS, 
		THEME_RID,
		FORECAST_MONITOR_ACTIVE, 
		FORECAST_MONITOR_DIRECTORY,
		MODIFY_SALES_MONITOR_ACTIVE, 
		MODIFY_SALES_MONITOR_DIRECTORY,
		AUDIT_LOGGING_LEVEL, 
		SHOW_LOGIN,
		SHOW_SIGNOFF_PROMPT,
		DCFULFILLMENT_MONITOR_ACTIVE, 
		DCFULFILLMENT_MONITOR_DIRECTORY
	FROM USER_OPTIONS
	WHERE USER_RID = @USER_RID

END
GO

