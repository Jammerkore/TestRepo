CREATE PROCEDURE [dbo].[SP_MID_TASKLIST_INSERT] 
	(
	@TASKLIST_NAME VARCHAR(50),
	@USER_RID INT,
	@SYSTEM_GENERATED_IND CHAR,
	@CREATED_BY_USER_RID INT,
	@CREATED_DATETIME DATETIME,
	@LAST_MODIFIED_BY_USER_RID INT,
	@LAST_MODIFIED_DATETIME DATETIME,
	@TASKLIST_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO TASKLIST (
		TASKLIST_NAME,
		USER_RID,
		SYSTEM_GENERATED_IND,
		CREATED_BY_USER_RID,
		CREATED_DATETIME
		)
	VALUES (
		@TASKLIST_NAME,
		@USER_RID,
		@SYSTEM_GENERATED_IND,
		@CREATED_BY_USER_RID,
		@CREATED_DATETIME
		)

	SELECT @TASKLIST_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


