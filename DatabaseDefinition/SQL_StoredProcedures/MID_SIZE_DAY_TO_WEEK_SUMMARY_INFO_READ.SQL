--dv =============================================
--dv Create date:	10/29/2013
--dv Description:	Reads rows from the size day to week summary info table based on the process rid
--dv =============================================
CREATE PROCEDURE [dbo].[MID_SIZE_DAY_TO_WEEK_SUMMARY_INFO_READ]
	@PROCESS_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		PROCESS_RID, 
		COALESCE(TOTAL_STYLES,0) TOTAL_STYLES,
		COALESCE(TOTAL_COLORS,0) TOTAL_COLORS,
		COALESCE(TOTAL_SIZES,0) TOTAL_SIZES,
		COALESCE(TOTAL_VALUES_READ,0) TOTAL_VALUES_READ,
		COALESCE(TOTAL_RECS_READ,0) TOTAL_RECS_READ,
		COALESCE(TOTAL_RECS_WRITTEN,0) TOTAL_RECS_WRITTEN,
		COALESCE(TOTAL_ERRORS,0) TOTAL_ERRORS
	FROM SIZE_DAY_TO_WEEK_SUMMARY_INFO 
	WHERE PROCESS_RID = @PROCESS_RID

END
GO
