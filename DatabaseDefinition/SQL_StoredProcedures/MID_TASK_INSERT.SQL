CREATE PROCEDURE [dbo].[MID_TASK_INSERT]
	@TASKLIST_RID int,
	@TASK_SEQUENCE int, 
	@TASK_TYPE int, 
	@MAX_MESSAGE_LEVEL int,
    @EMAIL_SUCCESS_FROM varchar(500), 
	@EMAIL_SUCCESS_TO varchar(500), 
	@EMAIL_SUCCESS_CC varchar(500), 
	@EMAIL_SUCCESS_BCC varchar(500), 
	@EMAIL_SUCCESS_SUBJECT varchar(500), 
	@EMAIL_SUCCESS_BODY varchar(4000),
    @EMAIL_FAILURE_FROM varchar(500), 
	@EMAIL_FAILURE_TO varchar(500), 
	@EMAIL_FAILURE_CC varchar(500), 
	@EMAIL_FAILURE_BCC varchar(500), 
	@EMAIL_FAILURE_SUBJECT varchar(500), 
	@EMAIL_FAILURE_BODY varchar(4000)
AS
BEGIN
	SET NOCOUNT ON;
	
	INSERT INTO TASK
		(
		TASKLIST_RID, 
		TASK_SEQUENCE, 
		TASK_TYPE, 
		MAX_MESSAGE_LEVEL,
        EMAIL_SUCCESS_FROM, 
		EMAIL_SUCCESS_TO, 
		EMAIL_SUCCESS_CC, 
		EMAIL_SUCCESS_BCC, 
		EMAIL_SUCCESS_SUBJECT, 
		EMAIL_SUCCESS_BODY,
        EMAIL_FAILURE_FROM, 
		EMAIL_FAILURE_TO,
		EMAIL_FAILURE_CC, 
		EMAIL_FAILURE_BCC, 
		EMAIL_FAILURE_SUBJECT, 
		EMAIL_FAILURE_BODY
		)
    VALUES
		(
		@TASKLIST_RID, 
		@TASK_SEQUENCE, 
		@TASK_TYPE, 
		@MAX_MESSAGE_LEVEL,
        @EMAIL_SUCCESS_FROM, 
		@EMAIL_SUCCESS_TO, 
		@EMAIL_SUCCESS_CC, 
		@EMAIL_SUCCESS_BCC, 
		@EMAIL_SUCCESS_SUBJECT, 
		@EMAIL_SUCCESS_BODY,
        @EMAIL_FAILURE_FROM, 
		@EMAIL_FAILURE_TO, 
		@EMAIL_FAILURE_CC, 
		@EMAIL_FAILURE_BCC, 
		@EMAIL_FAILURE_SUBJECT, 
		@EMAIL_FAILURE_BODY
		)

	SELECT @@ROWCOUNT;

END
GO