CREATE PROCEDURE [dbo].[SP_MID_GROUP_INSERT] 
	@GROUP_NAME VARCHAR(30),
	@GROUP_DESCRIPTION VARCHAR(250),
	@GROUP_ACTIVE_IND CHAR,
	@GROUP_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO USER_GROUP (
		GROUP_NAME,
		GROUP_DESCRIPTION,
		GROUP_ACTIVE_IND
		)
	VALUES (
		@GROUP_NAME,
		@GROUP_DESCRIPTION,
		@GROUP_ACTIVE_IND
		)

	SELECT @GROUP_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


