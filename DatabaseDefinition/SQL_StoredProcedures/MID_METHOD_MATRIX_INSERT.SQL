--dv =============================================
--dv Create date:	12/9/2013
--dv Description:	Inserts a row into the method matrix table
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_MATRIX_INSERT]
	@METHOD_RID int, 
	@FILTER_RID int, 
	@HN_RID int, 
	@HIGH_LEVEL_FV_RID int,
	@CDR_RID int, 
	@MODEL_RID int, 
	@MATRIX_TYPE int, 
	@LOW_LEVEL_FV_RID int, 
	@LOW_LEVEL_TYPE int, 
	@LOW_LEVEL_OFFSET int, 
	@LOW_LEVEL_SEQUENCE int, 
	@INCLUDE_INELIGIBLE_STORES_IND char(1), 
	@INCLUDE_SIMILAR_STORES_IND char(1), 
	@VARIABLE_NUMBER int, 
	@ITERATIONS_TYPE int, 
	@ITERATIONS_COUNT int, 
	@BALANCE_MODE int, 
	@CALC_MODE varchar(50), 
	@OLL_RID int
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO METHOD_MATRIX
		(
		METHOD_RID, 
		FILTER_RID, 
		HN_RID, 
		HIGH_LEVEL_FV_RID,
		CDR_RID, 
		MODEL_RID, 
		MATRIX_TYPE, 
		LOW_LEVEL_FV_RID, 
		LOW_LEVEL_TYPE, 
		LOW_LEVEL_OFFSET, 
		LOW_LEVEL_SEQUENCE, 
		INCLUDE_INELIGIBLE_STORES_IND, 
		INCLUDE_SIMILAR_STORES_IND, 
		VARIABLE_NUMBER, 
		ITERATIONS_TYPE, 
		ITERATIONS_COUNT, 
		BALANCE_MODE, 
		CALC_MODE, 
		OLL_RID
		)
	VALUES
		(
		@METHOD_RID, 
		@FILTER_RID, 
		@HN_RID, 
		@HIGH_LEVEL_FV_RID,
		@CDR_RID, 
		@MODEL_RID, 
		@MATRIX_TYPE, 
		@LOW_LEVEL_FV_RID, 
		@LOW_LEVEL_TYPE, 
		@LOW_LEVEL_OFFSET, 
		@LOW_LEVEL_SEQUENCE, 
		@INCLUDE_INELIGIBLE_STORES_IND, 
		@INCLUDE_SIMILAR_STORES_IND, 
		@VARIABLE_NUMBER, 
		@ITERATIONS_TYPE, 
		@ITERATIONS_COUNT, 
		@BALANCE_MODE, 
		@CALC_MODE, 
		@OLL_RID
		)

	SELECT @@ROWCOUNT

END
GO











