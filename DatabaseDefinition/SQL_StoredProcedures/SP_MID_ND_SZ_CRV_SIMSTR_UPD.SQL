CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_SIMSTR_UPD] 
	(
	@HN_RID INT,
	@ST_RID INT,
	@SS_RID INT,
	@UNTIL_DATE INT = NULL
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	UPDATE NODE_SIZE_CURVE_SIMILAR_STORE
	WITH (ROWLOCK)

	SET SS_RID = @SS_RID,
		UNTIL_DATE = @UNTIL_DATE
	WHERE HN_RID = @HN_RID
		AND ST_RID = @ST_RID
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


