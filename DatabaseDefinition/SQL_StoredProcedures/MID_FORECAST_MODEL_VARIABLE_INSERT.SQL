CREATE PROCEDURE [dbo].[MID_FORECAST_MODEL_VARIABLE_INSERT]
 @FORECAST_MOD_RID int,
 @VARIABLE_SEQUENCE int,
 @VARIABLE_NUMBER int,
 @FORECAST_FORMULA int,
 @ASSOC_VARIABLE int,
 @GRADE_WOS_IDX char(1),
 @STOCK_MODIFIER char(1),
 @FWOS_OVERRIDE char(1),
 @STOCK_MIN char(1),
 @STOCK_MAX char(1),
 @MIN_PLUS_SALES char(1),
 @SALES_MODIFIER char(1),
 @USE_PLAN char(1),
 @ALLOW_CHAIN_NEGATIVES char(1)

AS
BEGIN
	SET NOCOUNT ON;

INSERT INTO FORECAST_MODEL_VARIABLE
		(
		FORECAST_MOD_RID, 
		VARIABLE_SEQUENCE, 
		VARIABLE_NUMBER,
		FORECAST_FORMULA, 
		ASSOC_VARIABLE, 
		GRADE_WOS_IDX,
		STOCK_MODIFIER,
		FWOS_OVERRIDE, 
		STOCK_MIN, 
		STOCK_MAX,
		MIN_PLUS_SALES, 
		SALES_MODIFIER, 
		USE_PLAN, 
		ALLOW_CHAIN_NEGATIVES
		)
	VALUES 
		(
		@FORECAST_MOD_RID, 
		@VARIABLE_SEQUENCE, 
		@VARIABLE_NUMBER, 
		@FORECAST_FORMULA, 
		@ASSOC_VARIABLE, 
		@GRADE_WOS_IDX,
		@STOCK_MODIFIER, 
		@FWOS_OVERRIDE, 
		@STOCK_MIN, 
		@STOCK_MAX,
		@MIN_PLUS_SALES,
		@SALES_MODIFIER, 
		@USE_PLAN, 
		@ALLOW_CHAIN_NEGATIVES
		)

	SELECT @@ROWCOUNT
END
GO