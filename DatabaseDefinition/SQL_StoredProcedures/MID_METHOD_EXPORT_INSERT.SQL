	@LOW_LEVELS_ONLY_IND char(1),
	@LOW_LEVELS_TYPE int, 
	@LOW_LEVEL_SEQUENCE int, 
	@LOW_LEVEL_OFFSET int, 
	@SHOW_INELIGIBLE_IND char(1), 
	@USE_DEFAULT_SETTINGS_IND char(1), 
	@EXPORT_TYPE int,
	@DELIMETER char(1), 
	@CSV_FILE_EXTENSION varchar(20), 
	@DATE_TYPE int, 
	@PREINIT_VALUES_IND char(1), 
	@EXCLUDE_ZERO_VALUES_IND char(1), 
	@FILE_PATH varchar(250), 
	@ADD_DATE_STAMP_IND char(1), 
	@ADD_TIME_STAMP_IND char(1), 
	@SPLIT_TYPE int,
	@SPLIT_NUM_ENTRIES int, 
	@CONCURRENT_PROCESSES int, 
	@CREATE_FLAG_FILE_IND char(1), 
	@FLAG_FILE_EXTENSION varchar(20), 
	@CREATE_END_FILE_IND char(1), 
	@END_FILE_EXTENSION varchar(20), 
	@OLL_RID int
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO METHOD_EXPORT
		(
		METHOD_RID, 
		HN_RID, 
		FV_RID, 
		CDR_RID, 
		STORE_FILTER_RID, 
		PLAN_TYPE, 
		LOW_LEVELS_IND, 
		LOW_LEVELS_ONLY_IND,
		LOW_LEVELS_TYPE, 
		LOW_LEVEL_SEQUENCE, 
		LOW_LEVEL_OFFSET, 
		SHOW_INELIGIBLE_IND, 
		USE_DEFAULT_SETTINGS_IND, 
		EXPORT_TYPE,
		DELIMETER, 
		CSV_FILE_EXTENSION, 
		DATE_TYPE, 
		PREINIT_VALUES_IND, 
		EXCLUDE_ZERO_VALUES_IND, 
		FILE_PATH, 
		ADD_DATE_STAMP_IND, 
		ADD_TIME_STAMP_IND, 
		SPLIT_TYPE,
		SPLIT_NUM_ENTRIES, 
		CONCURRENT_PROCESSES, 
		CREATE_FLAG_FILE_IND, 
		FLAG_FILE_EXTENSION, 
		CREATE_END_FILE_IND, 
		END_FILE_EXTENSION, 
		OLL_RID
		)
	VALUES
		(
		@METHOD_RID, 
		@HN_RID, 
		@FV_RID, 
		@CDR_RID, 
		@STORE_FILTER_RID, 
		@PLAN_TYPE, 
		@LOW_LEVELS_IND, 
		@LOW_LEVELS_ONLY_IND,
		@LOW_LEVELS_TYPE, 
		@LOW_LEVEL_SEQUENCE, 
		@LOW_LEVEL_OFFSET, 
		@SHOW_INELIGIBLE_IND, 
		@USE_DEFAULT_SETTINGS_IND, 
		@EXPORT_TYPE,
		@DELIMETER, 
		@CSV_FILE_EXTENSION, 
		@DATE_TYPE, 
		@PREINIT_VALUES_IND, 
		@EXCLUDE_ZERO_VALUES_IND, 
		@FILE_PATH, 
		@ADD_DATE_STAMP_IND, 
		@ADD_TIME_STAMP_IND, 
		@SPLIT_TYPE,
		@SPLIT_NUM_ENTRIES, 
		@CONCURRENT_PROCESSES, 
		@CREATE_FLAG_FILE_IND, 
		@FLAG_FILE_EXTENSION, 
		@CREATE_END_FILE_IND, 
		@END_FILE_EXTENSION, 
		@OLL_RID
		)

	SELECT @@ROWCOUNT

END
GO