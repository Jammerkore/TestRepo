CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_OOS_DEL_ALL] 
	(@HN_RID INT)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	DELETE
	FROM NODE_SIZE_OUTOFSTOCK_QUANTITY
	WHERE HN_RID = @HN_RID

	DELETE
	FROM NODE_SIZE_OUTOFSTOCK_GRPLVL
	WHERE HN_RID = @HN_RID

	DELETE
	FROM NODE_SIZE_OUTOFSTOCK
	WHERE HN_RID = @HN_RID

	SELECT @@ROWCOUNT;

	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


