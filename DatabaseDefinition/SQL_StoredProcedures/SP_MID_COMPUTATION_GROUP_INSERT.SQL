CREATE PROCEDURE [dbo].[SP_MID_COMPUTATION_GROUP_INSERT] 
	(
	@PROCESS INT,
	@COMP_MODEL_RID INT,
	@COMP_GROUP_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO COMPUTATION_GROUP (
		PROCESS,
		COMP_MODEL_RID
		)
	VALUES (
		@PROCESS,
		@COMP_MODEL_RID
		)

	SELECT @COMP_GROUP_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


