--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_MATRIX_UPDATE]
	@METHOD_RID int, 
	@FILTER_RID int,
	@HN_RID int,
	@HIGH_LEVEL_FV_RID int,
	@CDR_RID int,
	@MATRIX_TYPE int,
	@MODEL_RID int,
	@LOW_LEVEL_FV_RID int,
	@LOW_LEVEL_TYPE int,
	@LOW_LEVEL_OFFSET int,
	@LOW_LEVEL_SEQUENCE int,
	@INCLUDE_INELIGIBLE_STORES_IND char(1),
	@INCLUDE_SIMILAR_STORES_IND char(1),
	@VARIABLE_NUMBER int,
	@ITERATIONS_TYPE int,
	@ITERATIONS_COUNT int,
	@BALANCE_MODE int,
	@CALC_MODE varchar(50),
	@OLL_RID int
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE METHOD_MATRIX 
	SET 
		FILTER_RID = @FILTER_RID,
		HN_RID = @HN_RID,
		HIGH_LEVEL_FV_RID = @HIGH_LEVEL_FV_RID,
		CDR_RID = @CDR_RID,
		MATRIX_TYPE = @MATRIX_TYPE,
		MODEL_RID = @MODEL_RID,
		LOW_LEVEL_FV_RID = @LOW_LEVEL_FV_RID,
		LOW_LEVEL_TYPE = @LOW_LEVEL_TYPE,
		LOW_LEVEL_OFFSET = @LOW_LEVEL_OFFSET,
		LOW_LEVEL_SEQUENCE = @LOW_LEVEL_SEQUENCE,
		INCLUDE_INELIGIBLE_STORES_IND = @INCLUDE_INELIGIBLE_STORES_IND,
		INCLUDE_SIMILAR_STORES_IND = @INCLUDE_SIMILAR_STORES_IND,
		VARIABLE_NUMBER = @VARIABLE_NUMBER,
		ITERATIONS_TYPE = @ITERATIONS_TYPE,
		ITERATIONS_COUNT = @ITERATIONS_COUNT,
		BALANCE_MODE = @BALANCE_MODE,
		CALC_MODE = @CALC_MODE,
		OLL_RID = @OLL_RID 
	WHERE METHOD_RID = @METHOD_RID

	SELECT @@ROWCOUNT

END
GO