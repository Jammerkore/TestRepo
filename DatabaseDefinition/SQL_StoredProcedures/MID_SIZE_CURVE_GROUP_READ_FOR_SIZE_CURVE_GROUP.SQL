CREATE PROCEDURE [dbo].[MID_SIZE_CURVE_GROUP_READ_FOR_SIZE_CURVE_GROUP]
	@SIZE_CURVE_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		SCG.SIZE_CURVE_GROUP_RID, 
		SIZE_CURVE_GROUP_NAME, 
		DEFAULT_SIZE_CURVE_RID, 
		DEFINED_SIZE_GROUP_RID, 
		ST_RID, 
		SSC.SIZE_CURVE_RID  
		
    FROM SIZE_CURVE_GROUP SCG, 
		STORE_SIZE_CURVE_BY_GROUP SSC 
	WHERE SCG.SIZE_CURVE_GROUP_RID = SSC.SIZE_CURVE_GROUP_RID
		AND SCG.SIZE_CURVE_GROUP_RID = @SIZE_CURVE_RID
END
GO