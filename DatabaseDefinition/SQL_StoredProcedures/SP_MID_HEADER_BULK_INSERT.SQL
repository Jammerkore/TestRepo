CREATE PROCEDURE [dbo].[SP_MID_HEADER_BULK_INSERT] 
	@HDR_RID INT,
	@COLOR_CODE_RID INT,
	@UNITS INT,
	@MULTIPLE INT,
	@MINIMUM INT,
	@MAXIMUM INT,
	@RESERVE_UNITS INT,
	@SEQ INT,
	@NAME VARCHAR(50),
	@DESCRIPTION VARCHAR(100),
	@ASRT_BC_RID INT,
	@LAST_BCSZ_KEY_USED INT,
	@COLOR_STATUS_FLAGS INT, /* TT#246 - MD - Jellis - AnF VSW In store Mins pt 5 */
	@HDR_BC_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HEADER_BULK_COLOR (
		HDR_RID,
		COLOR_CODE_RID,
		UNITS,
		MULTIPLE,
		MINIMUM,
		MAXIMUM,
		RESERVE_UNITS,
		SEQ,
		NAME,
		DESCRIPTION,
		ASRT_BC_RID,
		LAST_BCSZ_KEY_USED,
		COLOR_STATUS_FLAGS
		)
	VALUES (
		@HDR_RID,
		@COLOR_CODE_RID,
		@UNITS,
		@MULTIPLE,
		@MINIMUM,
		@MAXIMUM,
		@RESERVE_UNITS,
		@SEQ,
		@NAME,
		@DESCRIPTION,
		@ASRT_BC_RID,
		@LAST_BCSZ_KEY_USED,
		@COLOR_STATUS_FLAGS
		)

	SELECT @HDR_BC_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY() 
END
GO


