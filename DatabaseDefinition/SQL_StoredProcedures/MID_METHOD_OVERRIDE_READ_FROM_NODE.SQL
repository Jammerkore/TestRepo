CREATE PROCEDURE [dbo].[MID_METHOD_OVERRIDE_READ_FROM_NODE]
	@MERCH_HN_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		mo.METHOD_RID, 
		m.METHOD_NAME, 
		m.METHOD_TYPE_ID,
		m.USER_RID, 
		au.USER_NAME
	FROM METHOD_OVERRIDE mo, 
		 METHOD m, 
		 APPLICATION_USER au
	WHERE mo.MERCH_HN_RID = @MERCH_HN_RID
		  AND mo.METHOD_RID = m.METHOD_RID
		  AND m.USER_RID = au.USER_RID

END
GO