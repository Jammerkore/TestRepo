CREATE PROCEDURE [dbo].[SP_MID_SIZE_CURVE_INSERT] 
	@SIZE_CURVE_NAME VARCHAR(250),
	@SIZE_CURVE_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO SIZE_CURVE (SIZE_CURVE_NAME)
	VALUES (@SIZE_CURVE_NAME)

	SELECT @SIZE_CURVE_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


