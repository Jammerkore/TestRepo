CREATE PROCEDURE [dbo].[MID_GROUP_LEVEL_FUNCTION_READ_FROM_NODE]
	@SEASON_HN_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		glf.METHOD_RID, 
		m.METHOD_NAME, 
		m.METHOD_TYPE_ID,
		m.USER_RID, 
		au.USER_NAME
	FROM GROUP_LEVEL_FUNCTION glf, 
		 METHOD m, 
		 APPLICATION_USER au
	WHERE glf.SEASON_HN_RID = @SEASON_HN_RID
		  AND glf.METHOD_RID = m.METHOD_RID
		  AND m.USER_RID = au.USER_RID

END
GO