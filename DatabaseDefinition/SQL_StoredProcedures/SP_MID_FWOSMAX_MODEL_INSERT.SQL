CREATE PROCEDURE [dbo].[SP_MID_FWOSMAX_MODEL_INSERT] 
	@FWOSMAX_ID VARCHAR(50),
	@FWOSMAX_DEFAULT FLOAT,
	@FWOSMAX_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FWOS_MAX_MODEL (
		FWOSMAX_ID,
		FWOSMAX_DEFAULT
		)
	VALUES (
		@FWOSMAX_ID,
		@FWOSMAX_DEFAULT
		)

	SELECT @FWOSMAX_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


