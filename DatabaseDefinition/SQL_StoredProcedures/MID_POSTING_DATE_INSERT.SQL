CREATE PROCEDURE [dbo].[MID_POSTING_DATE_INSERT]
	@PH_RID int,
	@POSTINGDATE datetime,
	@POSTINGDATEYYYYDDD int,
	@POSTINGDATEOFFSET int,
	@CURRENTDATE datetime,
	@CURRENTDATEYYYYDDD int,
	@CURRENTDATEOFFSET int
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO POSTING_DATE(PH_RID, POSTING_DATE, POSTING_DATE_YYYYDDD, POSTING_DATE_OFFSET, CURR_DATE, CURR_DATE_YYYYDDD, CURR_DATE_OFFSET)
    VALUES 
	(
		@PH_RID, 
		@POSTINGDATE, 
		@POSTINGDATEYYYYDDD, 
		@POSTINGDATEOFFSET, 
		@CURRENTDATE, 
		@CURRENTDATEYYYYDDD, 
		@CURRENTDATEOFFSET 
	)

	SELECT @@ROWCOUNT;

END
GO
