CREATE PROCEDURE [dbo].[MID_METHOD_GLOBAL_LOCK_READ] 
	@METHOD_RID int
AS
BEGIN
	SET NOCOUNT ON;

	DECLARE @NoRID int = -1;
	DECLARE @UndefinedCalendarDateRange int = 1;

	SELECT 
	COALESCE(HN_RID, @NoRID) HN_RID,
	COALESCE(FV_RID, @NoRID) FV_RID,
	COALESCE(CDR_RID, @UndefinedCalendarDateRange) CDR_RID,
	COALESCE(MULTI_LEVEL_IND,'0') MULTI_LEVEL_IND,
	COALESCE(FROM_LEVEL_TYPE,'0') FROM_LEVEL_TYPE,
	COALESCE(FROM_LEVEL_OFFSET,'0') FROM_LEVEL_OFFSET,
	COALESCE(FROM_LEVEL_SEQ,'0') FROM_LEVEL_SEQ,
	COALESCE(TO_LEVEL_TYPE,'0') TO_LEVEL_TYPE,
	COALESCE(TO_LEVEL_OFFSET,'0') TO_LEVEL_OFFSET,
	COALESCE(TO_LEVEL_SEQ,'0') TO_LEVEL_SEQ,
	COALESCE(STORE_IND,'0') STORE_IND,
	COALESCE(CHAIN_IND,'0') CHAIN_IND,
	COALESCE(STORE_FILTER_RID,'0') STORE_FILTER_RID,
	COALESCE(OLL_RID, @NoRID) OLL_RID
    FROM METHOD_GLOBAL_LOCK 
	WHERE METHOD_RID = @METHOD_RID
	 
END
GO











