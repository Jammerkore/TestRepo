CREATE PROCEDURE [dbo].[SP_MID_CALENDARWEEK53_INSERT] 
	@WEEK53_FISCAL_YEAR INT,
	@CM_RID INT,
	@CMP_SEQUENCE INT,
	@OFFSET_ID INT,
	@CMP_TYPE INT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO CALENDAR_WEEK53_YEAR (
		WEEK53_FISCAL_YEAR,
		CM_RID,
		CMP_SEQUENCE,
		OFFSET_ID,
		CMP_TYPE
		)
	VALUES (
		@WEEK53_FISCAL_YEAR,
		@CM_RID,
		@CMP_SEQUENCE,
		@OFFSET_ID,
		@CMP_TYPE
		)

	SELECT @@ROWCOUNT;
END
GO


