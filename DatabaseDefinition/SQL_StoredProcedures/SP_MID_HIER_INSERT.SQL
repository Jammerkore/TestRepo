CREATE PROCEDURE [dbo].[SP_MID_HIER_INSERT] 
	@PH_ID VARCHAR(50),
	@PH_TYPE INT,
	@PH_COLOR VARCHAR(30),
	@PH_OWNER INT,
	@OTS_PLANLEVEL_TYPE INT,
	@HISTORY_ROLL_OPTION INT,
	@PH_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO PRODUCT_HIERARCHY (
		PH_ID,
		PH_TYPE,
		PH_COLOR,
		PH_OWNER,
		OTS_PLANLEVEL_TYPE,
		HISTORY_ROLL_OPTION
		)
	VALUES (
		@PH_ID,
		@PH_TYPE,
		@PH_COLOR,
		@PH_OWNER,
		@OTS_PLANLEVEL_TYPE,
		@HISTORY_ROLL_OPTION
		)

	SELECT @PH_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


