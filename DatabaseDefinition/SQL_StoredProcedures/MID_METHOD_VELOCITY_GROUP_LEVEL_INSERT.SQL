--dv Description:	Inserts a Velocity Group Level item
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_VELOCITY_GROUP_LEVEL_INSERT]
	@METHOD_RID int,
	@SGL_RID int,
	@NO_ONHAND_RULE int, 
	@NO_ONHAND_QUANTITY float,
	@MATRIX_MODE_IND char(1),
	@MATRIX_MODE_AVERAGE_RULE int,
	@MATRIX_MODE_AVERAGE_QUANTITY float,
	@MATRIX_SPREAD_IND char(1)
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO METHOD_VELOCITY_GROUP_LEVEL  
		(
		METHOD_RID, 
		SGL_RID, 
		NO_ONHAND_RULE, 
		NO_ONHAND_QUANTITY,
		MATRIX_MODE_IND,
		MATRIX_MODE_AVERAGE_RULE,
		MATRIX_MODE_AVERAGE_QUANITY,
		MATRIX_SPREAD_IND
		)
	VALUES
		(
		@METHOD_RID, 
		@SGL_RID, 
		@NO_ONHAND_RULE, 
		@NO_ONHAND_QUANTITY,
		@MATRIX_MODE_IND,
		@MATRIX_MODE_AVERAGE_RULE,
		@MATRIX_MODE_AVERAGE_QUANTITY,
		@MATRIX_SPREAD_IND
		)

	SELECT @@ROWCOUNT;
END
GO