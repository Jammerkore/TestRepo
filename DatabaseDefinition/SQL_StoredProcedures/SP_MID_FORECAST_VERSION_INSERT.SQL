CREATE PROCEDURE [dbo].[SP_MID_FORECAST_VERSION_INSERT] 
	@FV_ID CHAR,
	@DESCRIPTION VARCHAR(50),
	@PROTECT_HISTORY_IND CHAR,
	@ACTIVE_IND CHAR,
	@BLEND_TYPE INT,
	@ACTUAL_FV_RID INT,
	@FORECAST_FV_RID INT,
	@CURRENT_BLEND_IND CHAR,
	@SIMILAR_STORE_IND CHAR,
	@FV_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FORECAST_VERSION (
		FV_ID,
		DESCRIPTION,
		PROTECT_HISTORY_IND,
		ACTIVE_IND,
		BLEND_TYPE,
		ACTUAL_FV_RID,
		FORECAST_FV_RID,
		CURRENT_BLEND_IND,
		SIMILAR_STORE_IND
		)
	VALUES (
		@FV_ID,
		@DESCRIPTION,
		@PROTECT_HISTORY_IND,
		@ACTIVE_IND,
		@BLEND_TYPE,
		@ACTUAL_FV_RID,
		@FORECAST_FV_RID,
		@CURRENT_BLEND_IND,
		@SIMILAR_STORE_IND
		)

	SELECT @FV_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


