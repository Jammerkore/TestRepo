--dv Description:	Reads build packs for the given method
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_BLD_PACKS_READ]
	@METHOD_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT
		COALESCE(BPC_RID, -1) BPC_RID,
        COALESCE(PATTERN_COMP_MIN,-1) PATTERN_COMP_MIN,
        COALESCE(PATTERN_SIZE_MULTIPLE,0) PATTERN_SIZE_MULTIPLE,
        COALESCE(SIZE_GROUP_RID,-1) SIZE_GROUP_RID,
        COALESCE(SIZE_CURVE_GROUP_RID,-1) SIZE_CURVE_GROUP_RID,
        COALESCE(RESERVE_TOTAL,0) RESERVE_TOTAL,
        COALESCE(RESERVE_TOTAL_PERCENT_IND,'0') RESERVE_TOTAL_PERCENT_IND,
        COALESCE(RESERVE_BULK,0) RESERVE_BULK,
        COALESCE(RESERVE_BULK_PERCENT_IND, '0') RESERVE_BULK_PERCENT_IND,
        COALESCE(RESERVE_PACKS,0) RESERVE_PACKS,
        COALESCE(RESERVE_PACKS_PERCENT_IND, '0') RESERVE_PACKS_PERCENT_IND,
        AVG_PACK_DEV_TOLERANCE, MAX_PACK_NEED_TOLERANCE,               
        COALESCE(DEPLETE_RESERVE_SELECTED,'1') DEPLETE_RESERVE_SELECTED,
        COALESCE(INCREASE_BUY_SELECTED,'0') INCREASE_BUY_SELECTED,    
        INCREASE_BUY_PCT,          
        COALESCE(REMOVE_BULK_IND,'0') REMOVE_BULK_IND
	FROM METHOD_BLD_PACKS 
	WHERE METHOD_RID=@METHOD_RID

END
GO