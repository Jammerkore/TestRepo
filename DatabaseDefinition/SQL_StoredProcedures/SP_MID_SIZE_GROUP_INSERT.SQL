CREATE PROCEDURE [dbo].[SP_MID_SIZE_GROUP_INSERT] 
	@SIZE_GROUP_NAME VARCHAR(50),
	@SIZE_GROUP_DESCRIPTION VARCHAR(250),
	@WIDTH_ACROSS_IND CHAR,
	@SIZE_GROUP_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO SIZE_GROUP (
		SIZE_GROUP_NAME,
		SIZE_GROUP_DESCRIPTION,
		WIDTH_ACROSS_IND
		)
	VALUES (
		@SIZE_GROUP_NAME,
		@SIZE_GROUP_DESCRIPTION,
		@WIDTH_ACROSS_IND
		)

	SELECT @SIZE_GROUP_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


