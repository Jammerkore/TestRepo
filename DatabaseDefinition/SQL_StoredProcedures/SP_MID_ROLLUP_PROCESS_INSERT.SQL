CREATE PROCEDURE [dbo].[SP_MID_ROLLUP_PROCESS_INSERT] 
	@PROCESS_RID INT,
	@ROLLUP_TYPE INT,
	@STATUS_CODE INT,
	@BATCH_NUMBER INT,
	@START_TIME DATETIME,
	@ROLLUP_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO ROLLUP_PROCESS (
		PROCESS_RID,
		ROLLUP_TYPE,
		STATUS_CODE,
		BATCH_NUMBER,
		START_TIME
		)
	VALUES (
		@PROCESS_RID,
		@ROLLUP_TYPE,
		@STATUS_CODE,
		@BATCH_NUMBER,
		@START_TIME
		)

	SELECT @ROLLUP_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


