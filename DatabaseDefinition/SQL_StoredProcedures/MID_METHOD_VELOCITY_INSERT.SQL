CREATE PROCEDURE [dbo].[MID_METHOD_VELOCITY_INSERT]
	@METHOD_RID int,
	@SG_RID int,
	@OTS_PLAN_HN_RID int,
	@OTS_PLAN_PH_RID int,
	@OTS_PLAN_PHL_SEQUENCE int,
	@SIM_STORE_IND char(1),
	@AVG_USING_CHAIN_IND char(1),
	@SHIP_USING_BASIS_IND char(1),
	@TREND_PERCENT char(1),
	@OTS_BEGIN_CDR_RID int,
	@OTS_SHIP_TO_CDR_RID int,
	@BALANCE_IND char(1),
	@APPLY_MIN_MAX_IND char(1),
	@RECONCILE_IND char(1),
	@INVENTORY_IND char(1),
	@MERCH_TYPE int,
	@MERCH_HN_RID int,
	@MERCH_PH_RID int,
	@MERCH_PHL_SEQUENCE int,
	@GRADE_VARIABLE_TYPE int,
	@BALANCE_TO_HEADER_IND char(1)
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO METHOD_VELOCITY  
		(
		METHOD_RID, 
		SG_RID,
		OTS_PLAN_HN_RID,
		OTS_PLAN_PH_RID,
		OTS_PLAN_PHL_SEQUENCE,
		SIM_STORE_IND,
		AVG_USING_CHAIN_IND,
		SHIP_USING_BASIS_IND,
		TREND_PERCENT,
		OTS_BEGIN_CDR_RID,
		OTS_SHIP_TO_CDR_RID,
		BALANCE_IND,
		APPLY_MIN_MAX_IND,
		RECONCILE_IND,
		INVENTORY_IND, 
		MERCH_TYPE,
		MERCH_HN_RID,
		MERCH_PH_RID,
		MERCH_PHL_SEQUENCE,
		GRADE_VARIABLE_TYPE,
		BALANCE_TO_HEADER_IND
		)
	VALUES
		(
		@METHOD_RID, 
		@SG_RID,
		@OTS_PLAN_HN_RID,
		@OTS_PLAN_PH_RID,
		@OTS_PLAN_PHL_SEQUENCE,
		@SIM_STORE_IND,
		@AVG_USING_CHAIN_IND,
		@SHIP_USING_BASIS_IND,
		@TREND_PERCENT,
		@OTS_BEGIN_CDR_RID,
		@OTS_SHIP_TO_CDR_RID,
		@BALANCE_IND,
		@APPLY_MIN_MAX_IND,
		@RECONCILE_IND,
		@INVENTORY_IND, 
		@MERCH_TYPE,
		@MERCH_HN_RID,
		@MERCH_PH_RID,
		@MERCH_PHL_SEQUENCE,
		@GRADE_VARIABLE_TYPE,
		@BALANCE_TO_HEADER_IND
		)

	SELECT @@ROWCOUNT;
END
GO