CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_RECEIPT_DATES]
	@PURGE_HEADERS_RECEIPT_DATETIME datetime,
	@PURGE_HEADERS_RECEIPT INT,
	@PURGE_HEADERS_WEEKS_RECEIPT INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_RECEIPT_DATETIME = @PURGE_HEADERS_RECEIPT_DATETIME,
		PURGE_HEADERS_RECEIPT = @PURGE_HEADERS_RECEIPT
	WHERE PURGE_HEADERS_WEEKS_RECEIPT = @PURGE_HEADERS_WEEKS_RECEIPT

	SELECT @@ROWCOUNT
END
GO




