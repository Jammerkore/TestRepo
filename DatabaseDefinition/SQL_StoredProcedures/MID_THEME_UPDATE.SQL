--dv Description: Updates a theme.
--dv =============================================
CREATE PROCEDURE [dbo].[MID_THEME_UPDATE]
	@THEME_RID INT,
	@THEMENAME VARCHAR(50),
	@VIEWSTYLE INT,
	@CELLBORDERSTYLE INT,
	@CELLBORDERCOLOR INT,
	@DIVWIDTH INT,
	@DISPLAYROWGROUPDIV CHAR(1),
	@ROWGRPROWHDRDIVCOLOR INT,
	@ROWGRPROWHDRDIVBRUSHCOLOR INT,
	@ROWGRPDIVCOLOR INT,
	@ROWGRPDIVBRUSHCOLOR INT,
	@DISPLAYCOLUMNGROUPDIV CHAR(1),
	@COLGRPDIVCOLOR INT,
	@COLGRPDIVBRUSHCOLOR INT,
	@NODEDESCFONTFAMILY VARCHAR(50),
	@NODEDESCFONTSIZE FLOAT,
	@NODEDESCFONTSTYLE INT,
	@NODEDESCTEXTEFFECTS INT,
	@COLGRPHDRFONTFAMILY VARCHAR(50),
	@COLGRPHDRFONTSIZE FLOAT,
	@COLGRPHDRFONTSTYLE INT,
	@COLGRPHDRTEXTEFFECTS INT,
	@COLHDRFONTFAMILY VARCHAR(50),
	@COLHDRFONTSIZE FLOAT,
	@COLHDRFONTSTYLE INT,
	@COLHDRTEXTEFFECTS INT,
	@ROWHDRFONTFAMILY VARCHAR(50),
	@ROWHDRFONTSIZE FLOAT,
	@ROWHDRFONTSTYLE INT,
	@DISPLAYONLYFONTFAMILY VARCHAR(50),
	@DISPLAYONLYFONTSIZE FLOAT,
	@DISPLAYONLYFONTSTYLE INT,
	@EDITABLEFONTFAMILY VARCHAR(50),
	@EDITABLEFONTSIZE FLOAT,
	@EDITABLEFONTSTYLE INT,
	@INELIGSTRFONTFAMILY VARCHAR(50),
	@INELIGSTRFONTSIZE FLOAT,
	@INELIGSTRFONTSTYLE INT,
	@LOCKEDFONTFAMILY VARCHAR(50),
	@LOCKEDFONTSIZE FLOAT,
	@LOCKEDFONTSTYLE INT,
	@NODEDESCFORECOLOR INT,
	@COLGRPHDRFORECOLOR INT,
	@COLHDRFORECOLOR INT,
	@NEGATIVEFORECOLOR INT,
	@NODEDESCBACKCOLOR INT,
	@COLGRPHDRBACKCOLOR INT,
	@COLHDRBACKCOLOR INT,
	@STRDETROWHEADERFORECOLOR INT,
	@STRDETFORECOLOR INT,
	@STRSETROWHEADERFORECOLOR INT,
	@STRSETFORECOLOR INT,
	@STRTOTROWHDRFORECOLOR INT,
	@STRTOTALFORECOLOR INT,
	@STRDETROWHEADERBACKCOLOR INT,
	@STRDETROWHEADERALTCOLOR INT,
	@STRDETBACKCOLOR INT,
	@STRDETALTCOLOR INT,
	@STRSETROWHEADERBACKCOLOR INT,
	@STRSETROWHEADERALTCOLOR INT,
	@STRSETBACKCOLOR INT,
	@STRSETALTCOLOR INT,
	@STRTOTROWHDRBACKCOLOR INT,
	@STRTOTROWHDRALTCOLOR INT,
	@STRTOTALBACKCOLOR INT,
	@STRTOTALALTCOLOR INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE THEME
	SET 
		THEMENAME = @THEMENAME,
		VIEWSTYLE = @VIEWSTYLE,
		CELLBORDERSTYLE = @CELLBORDERSTYLE,
		CELLBORDERCOLOR = @CELLBORDERCOLOR,
		DIVWIDTH = @DIVWIDTH,
		DISPLAYROWGROUPDIV = @DISPLAYROWGROUPDIV,
		ROWGROUPROWHEADERDIVCOLOR = @ROWGRPROWHDRDIVCOLOR,
		ROWGROUPROWHEADERDIVBRUSHCOLOR = @ROWGRPROWHDRDIVBRUSHCOLOR,
		ROWGROUPDIVCOLOR = @ROWGRPDIVCOLOR,
		ROWGROUPDIVBRUSHCOLOR = @ROWGRPDIVBRUSHCOLOR,
		DISPLAYCOLUMNGROUPDIV = @DISPLAYCOLUMNGROUPDIV,
		COLUMNGROUPDIVCOLOR = @COLGRPDIVCOLOR,
		COLUMNGROUPDIVBRUSHCOLOR = @COLGRPDIVBRUSHCOLOR,
		NODEDESCRIPTIONFONTFAMILY = @NODEDESCFONTFAMILY,
		NODEDESCRIPTIONFONTSIZE = @NODEDESCFONTSIZE,
		NODEDESCRIPTIONFONTSTYLE = @NODEDESCFONTSTYLE,
		NODEDESCRIPTIONTEXTEFFECTS = @NODEDESCTEXTEFFECTS,
		COLUMNGROUPHEADERFONTFAMILY = @COLGRPHDRFONTFAMILY,
		COLUMNGROUPHEADERFONTSIZE = @COLGRPHDRFONTSIZE,
		COLUMNGROUPHEADERFONTSTYLE = @COLGRPHDRFONTSTYLE,
		COLUMNGROUPHEADERTEXTEFFECTS = @COLGRPHDRTEXTEFFECTS,
		COLUMNHEADERFONTFAMILY = @COLHDRFONTFAMILY,
		COLUMNHEADERFONTSIZE = @COLHDRFONTSIZE,
		COLUMNHEADERFONTSTYLE = @COLHDRFONTSTYLE,
		COLUMNHEADERTEXTEFFECTS = @COLHDRTEXTEFFECTS,
		ROWHEADERFONTFAMILY = @ROWHDRFONTFAMILY,
		ROWHEADERFONTSIZE = @ROWHDRFONTSIZE,
		ROWHEADERFONTSTYLE = @ROWHDRFONTSTYLE,
		DISPLAYONLYFONTFAMILY = @DISPLAYONLYFONTFAMILY,
		DISPLAYONLYFONTSIZE = @DISPLAYONLYFONTSIZE,
		DISPLAYONLYFONTSTYLE = @DISPLAYONLYFONTSTYLE,
		EDITABLEFONTFAMILY = @EDITABLEFONTFAMILY,
		EDITABLEFONTSIZE = @EDITABLEFONTSIZE,
		EDITABLEFONTSTYLE = @EDITABLEFONTSTYLE,
		INELIGIBLESTOREFONTFAMILY = @INELIGSTRFONTFAMILY,
		INELIGIBLESTOREFONTSIZE = @INELIGSTRFONTSIZE,
		INELIGIBLESTOREFONTSTYLE = @INELIGSTRFONTSTYLE,
		LOCKEDFONTFAMILY = @LOCKEDFONTFAMILY,
		LOCKEDFONTSIZE = @LOCKEDFONTSIZE,
		LOCKEDFONTSTYLE = @LOCKEDFONTSTYLE,
		NODEDESCRIPTIONFORECOLOR = @NODEDESCFORECOLOR,
		COLUMNGROUPHEADERFORECOLOR = @COLGRPHDRFORECOLOR,
		COLUMNHEADERFORECOLOR = @COLHDRFORECOLOR,
		NEGATIVEFORECOLOR = @NEGATIVEFORECOLOR,
		NODEDESCRIPTIONBACKCOLOR = @NODEDESCBACKCOLOR,
		COLUMNGROUPHEADERBACKCOLOR = @COLGRPHDRBACKCOLOR,
		COLUMNHEADERBACKCOLOR = @COLHDRBACKCOLOR,
		STOREDETAILROWHEADERFORECOLOR = @STRDETROWHEADERFORECOLOR,
		STOREDETAILFORECOLOR = @STRDETFORECOLOR,
		STORESETROWHEADERFORECOLOR = @STRSETROWHEADERFORECOLOR,
		STORESETFORECOLOR = @STRSETFORECOLOR,
		STORETOTALROWHEADERFORECOLOR = @STRTOTROWHDRFORECOLOR,
		STORETOTALFORECOLOR = @STRTOTALFORECOLOR,
		STOREDETAILROWHEADERBACKCOLOR = @STRDETROWHEADERBACKCOLOR,
		STOREDETAILROWHEADERALTCOLOR = @STRDETROWHEADERALTCOLOR,
		STOREDETAILBACKCOLOR = @STRDETBACKCOLOR,
		STOREDETAILALTBACKCOLOR = @STRDETALTCOLOR,
		STORESETROWHEADERBACKCOLOR = @STRSETROWHEADERBACKCOLOR,
		STORESETROWHEADERALTCOLOR = @STRSETROWHEADERALTCOLOR,
		STORESETBACKCOLOR = @STRSETBACKCOLOR,
		STORESETALTBACKCOLOR = @STRSETALTCOLOR,
		STORETOTALROWHEADERBACKCOLOR = @STRTOTROWHDRBACKCOLOR,
		STORETOTALROWHEADERALTCOLOR = @STRTOTROWHDRALTCOLOR,
		STORETOTALBACKCOLOR = @STRTOTALBACKCOLOR,
		STORETOTALALTCOLOR = @STRTOTALALTCOLOR
	WHERE THEME_RID = @THEME_RID

	SELECT @@ROWCOUNT

END
GO