CREATE PROCEDURE [dbo].[MID_APPLICATION_USER_READ_ALL_FOR_MAINTENANCE]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		USER_RID, 
		USER_NAME, 
		COALESCE(USER_FULLNAME,' ') USER_FULLNAME, 
		COALESCE(USER_DESCRIPTION,' ') USER_DESCRIPTION, 
		USER_ACTIVE_IND,
		COALESCE(USER_DELETE_IND,'0') USER_DELETE_IND, 
		USER_DELETE_DATETIME 
	FROM APPLICATION_USER 
	WHERE USER_RID > 1 
	ORDER BY USER_ACTIVE_IND DESC, USER_NAME

END
GO