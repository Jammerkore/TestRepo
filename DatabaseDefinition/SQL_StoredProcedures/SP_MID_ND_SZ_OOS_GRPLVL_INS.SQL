CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_OOS_GRPLVL_INS] 
	(
	@HN_RID INT,
	@SGL_RID INT,
	@ROW_TYPE_ID INT,
	@OOS_QUANTITY INT = NULL
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	BEGIN
		INSERT INTO NODE_SIZE_OUTOFSTOCK_GRPLVL (
			HN_RID,
			SGL_RID,
			ROW_TYPE_ID,
			OOS_QUANTITY
			)
		VALUES (
			@HN_RID,
			@SGL_RID,
			@ROW_TYPE_ID,
			@OOS_QUANTITY
			)
	END
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


