CREATE PROCEDURE [dbo].[SP_MID_HEADER_PACKCOLOR_INSERT] 
	@HDR_PACK_RID INT,
	@COLOR_CODE_RID INT,
	@UNITS INT,
	@SEQ INT,
	@NAME VARCHAR(50),
	@DESCRIPTION VARCHAR(100),
	@LAST_PCSZ_KEY_USED INT,
	@HDR_PC_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HEADER_PACK_COLOR (
		HDR_PACK_RID,
		COLOR_CODE_RID,
		UNITS,
		SEQ,
		NAME,
		DESCRIPTION,
		LAST_PCSZ_KEY_USED
		)
	VALUES (
		@HDR_PACK_RID,
		@COLOR_CODE_RID,
		@UNITS,
		@SEQ,
		@NAME,
		@DESCRIPTION,
		@LAST_PCSZ_KEY_USED
		)

	SELECT @HDR_PC_RID = SCOPE_IDENTITY() 
END
GO


