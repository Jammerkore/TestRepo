CREATE PROCEDURE [dbo].[MID_STORES_READ_FOR_DELETION]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT ST_RID,
		COALESCE(ST_ID, '') ST_ID,
		COALESCE(STORE_NAME, '') STORE_NAME,
		COALESCE(STORE_DESC, '') STORE_DESC,
		ACTIVE_IND,
		COALESCE(CITY, '') CITY,
		COALESCE(STATE, '') STATE,
		COALESCE(SELLING_SQ_FT, 0) SELLING_SQ_FT,
		SELLING_OPEN_DATE,
		SELLING_CLOSE_DATE,
		STOCK_OPEN_DATE,
		STOCK_CLOSE_DATE,
		COALESCE(LEAD_TIME, 0) LEAD_TIME,
		SHIP_ON_MONDAY,
		SHIP_ON_TUESDAY,
		SHIP_ON_WEDNESDAY,
		SHIP_ON_THURSDAY,
		SHIP_ON_FRIDAY,
		SHIP_ON_SATURDAY,
		SHIP_ON_SUNDAY,
		SIMILAR_STORE_MODEL,
		IMO_ID,
		STORE_DELETE_IND
	FROM STORES
	WHERE STORE_DELETE_IND = '1'
	ORDER BY ST_RID

END
GO