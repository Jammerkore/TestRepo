CREATE PROCEDURE [dbo].[SP_MID_HEADERCHAR_INSERT] 
	@HCG_RID INT,
	@TEXT_VALUE VARCHAR(500),
	@DATE_VALUE SMALLDATETIME,
	@NUMBER_VALUE FLOAT,
	@DOLLAR_VALUE FLOAT,
	@HC_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HEADER_CHAR (
		HCG_RID,
		TEXT_VALUE,
		DATE_VALUE,
		NUMBER_VALUE,
		DOLLAR_VALUE
		)
	VALUES (
		@HCG_RID,
		@TEXT_VALUE,
		@DATE_VALUE,
		@NUMBER_VALUE,
		@DOLLAR_VALUE
		)

	SELECT @HC_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


