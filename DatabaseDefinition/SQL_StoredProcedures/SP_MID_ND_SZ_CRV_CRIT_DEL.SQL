CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_CRIT_DEL] 
	(
	@HN_RID INT,
	@NSCCD_RID INT
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	DELETE NODE_SIZE_CURVE_CRITERIA
	WITH (ROWLOCK)
	WHERE HN_RID = @HN_RID
		AND NSCCD_RID = @NSCCD_RID

	DELETE NODE_SIZE_CURVE_CRITERIA_DEFAULT
	WITH (ROWLOCK)
	WHERE NSCCD_RID = @NSCCD_RID

	DELETE NODE_SIZE_CURVE_CRITERIA_DETAIL
	WITH (ROWLOCK)
	WHERE NSCCD_RID = @NSCCD_RID

	SELECT @@ROWCOUNT;

	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


