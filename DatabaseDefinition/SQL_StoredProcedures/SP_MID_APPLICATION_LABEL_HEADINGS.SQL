CREATE PROCEDURE [dbo].[SP_MID_APPLICATION_LABEL_HEADINGS] 
	@inUseType INT
AS
BEGIN
	SELECT at.TEXT_VALUE
	FROM APPLICATION_LABELS AS iul
	INNER JOIN APPLICATION_TEXT AS at ON iul.APPLICATION_LABEL_HEADING = at.TEXT_CODE
	WHERE (iul.APPLICATION_LABEL_TYPE = @inUseType)
	ORDER BY iul.SEQUENCE
END
GO


