--dv Create date:	8/13/2014
--dv Description:	Reads rows from the posting info table based on process rid passed in
--dv =============================================
CREATE PROCEDURE [dbo].[MID_POSTING_INFO_READ]
	@PROCESS_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		PROCESS_RID, 
		COALESCE(CH_DAY_HIS_RECS,0) CH_DAY_HIS_RECS, 
		COALESCE(CH_WK_HIS_RECS,0) CH_WK_HIS_RECS,
		COALESCE(CH_WK_FOR_RECS,0) CH_WK_FOR_RECS,
		COALESCE(ST_DAY_HIS_RECS,0) ST_DAY_HIS_RECS,
		COALESCE(ST_WK_HIS_RECS,0) ST_WK_HIS_RECS,
		COALESCE(ST_WK_FOR_RECS,0) ST_WK_FOR_RECS,
		COALESCE(RECS_WITH_ERRORS,0) RECS_WITH_ERRORS,
		COALESCE(INTRANSIT_RECS,0) INTRANSIT_RECS,
		COALESCE(NODES_ADDED,0) NODES_ADDED
	FROM POSTING_INFO
	WHERE PROCESS_RID=@PROCESS_RID

END
GO
