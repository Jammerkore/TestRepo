--dv Description:	Inserts a header reconcile task
--dv =============================================
CREATE PROCEDURE [dbo].[MID_TASK_HEADER_RECONCILE_INSERT]
	@TASKLIST_RID int,
	@TASK_SEQUENCE int, 
	@INPUT_DIRECTORY varchar(400), 
	@OUTPUT_DIRECTORY varchar(400), 
	@TRIGGER_SUFFIX varchar(50), 
	@REMOVE_TRANS_FILE_NAME varchar(50), 
	@REMOVE_TRANS_TRIGGER_SUFFIX varchar(50), 
	@HEADER_TYPES varchar(400), 
	@HEADER_KEYS_FILE_NAME varchar(400)

AS
BEGIN
	SET NOCOUNT ON;
	
	INSERT INTO TASK_HEADER_RECONCILE
		(
		TASKLIST_RID, 
		TASK_SEQUENCE, 
		INPUT_DIRECTORY, 
		OUTPUT_DIRECTORY, 
		TRIGGER_SUFFIX, 
		REMOVE_TRANS_FILE_NAME, 
		REMOVE_TRANS_TRIGGER_SUFFIX, 
		HEADER_TYPES, 
		HEADER_KEYS_FILE_NAME
		)
    VALUES
		(
		@TASKLIST_RID, 
		@TASK_SEQUENCE, 
		@INPUT_DIRECTORY, 
		@OUTPUT_DIRECTORY, 
		@TRIGGER_SUFFIX, 
		@REMOVE_TRANS_FILE_NAME, 
		@REMOVE_TRANS_TRIGGER_SUFFIX, 
		@HEADER_TYPES, 
		@HEADER_KEYS_FILE_NAME
		)

	SELECT @@ROWCOUNT;

END
GO
