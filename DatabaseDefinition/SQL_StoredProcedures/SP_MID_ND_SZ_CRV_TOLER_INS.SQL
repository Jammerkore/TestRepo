CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_TOLER_INS] 
	(
	@HN_RID INT,
	@MINIMUM_AVERAGE FLOAT,
	@PH_OFFSET_IND CHAR(1) = NULL,
	@PH_RID INT = NULL,
	@PHL_SEQUENCE INT = NULL,
	@PHL_OFFSET INT = NULL,
	@SALES_TOLERANCE FLOAT = NULL,
	@INDEX_UNITS_TYPE INT = NULL,
	@MIN_TOLERANCE FLOAT = NULL,
	@MAX_TOLERANCE FLOAT = NULL,
	@APPLY_MIN_TO_ZERO_TOLERANCE_IND CHAR = NULL
	) -- TT#2079 - DOConnell - Size - Minimum % Tolerance Calculation Issue)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	INSERT INTO NODE_SIZE_CURVE_TOLERANCE (
		HN_RID,
		MINIMUM_AVERAGE,
		PH_OFFSET_IND,
		PH_RID,
		PHL_SEQUENCE,
		PHL_OFFSET,
		SALES_TOLERANCE,
		INDEX_UNITS_TYPE,
		MIN_TOLERANCE,
		MAX_TOLERANCE,
		APPLY_MIN_TO_ZERO_TOLERANCE_IND
		) -- TT#2079 - DOConnell - Size - Minimum % Tolerance Calculation Issue))
	VALUES (
		@HN_RID,
		@MINIMUM_AVERAGE,
		@PH_OFFSET_IND,
		@PH_RID,
		@PHL_SEQUENCE,
		@PHL_OFFSET,
		@SALES_TOLERANCE,
		@INDEX_UNITS_TYPE,
		@MIN_TOLERANCE,
		@MAX_TOLERANCE,
		@APPLY_MIN_TO_ZERO_TOLERANCE_IND
		) -- TT#2079 - DOConnell - Size - Minimum % Tolerance Calculation Issue))
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF

END
GO


