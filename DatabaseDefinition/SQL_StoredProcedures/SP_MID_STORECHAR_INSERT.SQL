CREATE PROCEDURE [dbo].[SP_MID_STORECHAR_INSERT] 
	@SCG_RID INT,
	@TEXT_VALUE VARCHAR(50),
	@DATE_VALUE SMALLDATETIME,
	@NUMBER_VALUE FLOAT,
	@DOLLAR_VALUE FLOAT,
	@SC_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO STORE_CHAR (
		SCG_RID,
		TEXT_VALUE,
		DATE_VALUE,
		NUMBER_VALUE,
		DOLLAR_VALUE
		)
	VALUES (
		@SCG_RID,
		@TEXT_VALUE,
		@DATE_VALUE,
		@NUMBER_VALUE,
		@DOLLAR_VALUE
		)

	SELECT @SC_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


