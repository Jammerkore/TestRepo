CREATE PROCEDURE [dbo].[MID_METHOD_SIZE_NEED_READ]
	@METHOD_RID int

AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		METHOD_RID, 
		SIZE_GROUP_RID, 
		SIZE_CURVE_GROUP_RID, 
		MERCH_HN_RID, 
		MERCH_PH_RID, 
		MERCH_PHL_SEQUENCE, 
		AVG_PACK_DEV_TOLERANCE, 
		MAX_PACK_NEED_TOLERANCE, 
		MERCH_TYPE, 
		SIZE_ALTERNATE_RID, 
		SIZE_CONSTRAINT_RID, 
		GENCURVE_HCG_RID, 
		GENCURVE_HN_RID, 
		GENCURVE_PH_RID, 
		GENCURVE_PHL_SEQUENCE, 
		COALESCE(GENCURVE_COLOR_IND, '0') GENCURVE_COLOR_IND, 
		GENCURVE_MERCH_TYPE, 
		NORMALIZE_SIZE_CURVES_IND, 
		USE_DEFAULT_CURVE_IND, 
		APPLY_RULES_ONLY_IND, 
		GENCURVE_NSCCD_RID, 
		GENCONSTRAINT_HCG_RID, 
		GENCONSTRAINT_HN_RID, 
		GENCONSTRAINT_PH_RID, 
		GENCONSTRAINT_PHL_SEQUENCE, 
		COALESCE(GENCONSTRAINT_COLOR_IND, '0') GENCONSTRAINT_COLOR_IND, 
		GENCONSTRAINT_MERCH_TYPE, 
		OVERRIDE_AVG_PACK_DEV_IND, 
		OVERRIDE_MAX_PACK_NEED_IND, 
		COALESCE(PACK_TOLERANCE_NO_MAX_STEP_IND, '0') PACK_TOLERANCE_NO_MAX_STEP_IND, 
		COALESCE(PACK_TOLERANCE_STEPPED_IND, '0') PACK_TOLERANCE_STEPPED_IND, 
		IB_MERCH_TYPE, 
		IB_MERCH_HN_RID, 
		IB_MERCH_PH_RID, 
		IB_MERCH_PHL_SEQUENCE, 
		VSW_SIZE_CONSTRAINTS_IND, 
		VSW_SIZE_CONSTRAINTS

    FROM METHOD_SIZE_NEED 
	WHERE METHOD_RID = @METHOD_RID

END
GO