CREATE PROCEDURE [dbo].[SP_MID_SPECIAL_REQ_JOB_INSERT] 
	(
	@SPECIAL_REQ_NAME VARCHAR(50),
	@CONCURRENT_PROCESSES INT,
	@SPECIAL_REQ_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO SPECIAL_REQUEST_JOB (
		SPECIAL_REQ_NAME,
		CONCURRENT_PROCESSES
		)
	VALUES (
		@SPECIAL_REQ_NAME,
		@CONCURRENT_PROCESSES
		)

	SELECT @SPECIAL_REQ_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


