CREATE PROCEDURE [dbo].[SP_MID_SIZE_CURVE_GROUP_INSERT] 
	@SIZE_CURVE_GROUP_NAME VARCHAR(250),
	@DEFAULT_SIZE_CURVE_RID INT,
	@DEFINED_SIZE_GROUP_RID INT,
	@SIZE_CURVE_GROUP_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO SIZE_CURVE_GROUP (
		SIZE_CURVE_GROUP_NAME,
		DEFAULT_SIZE_CURVE_RID,
		DEFINED_SIZE_GROUP_RID
		)
	VALUES (
		@SIZE_CURVE_GROUP_NAME,
		@DEFAULT_SIZE_CURVE_RID,
		@DEFINED_SIZE_GROUP_RID
		)

	SELECT @SIZE_CURVE_GROUP_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


