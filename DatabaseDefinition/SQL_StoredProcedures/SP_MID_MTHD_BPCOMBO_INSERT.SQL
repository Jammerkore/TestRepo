CREATE PROCEDURE [dbo].[SP_MID_MTHD_BPCOMBO_INSERT] 
	@METHOD_RID INT,
	@COMBO_NAME VARCHAR(50),
	@COMBO_SELECTED_IND CHAR,
	@COMBO_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO METHOD_BLD_PACKS_COMBO (
		METHOD_RID,
		COMBO_NAME,
		COMBO_SELECTED_IND
		)
	VALUES (
		@METHOD_RID,
		@COMBO_NAME,
		@COMBO_SELECTED_IND
		)

	SELECT @COMBO_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


