CREATE PROCEDURE [dbo].[SP_MID_SLSMOD_MODEL_INSERT] 
	@SLSMOD_ID VARCHAR(50),
	@SLSMOD_DEFAULT FLOAT,
	@SLSMOD_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO SALES_MODIFIER_MODEL (
		SLSMOD_ID,
		SLSMOD_DEFAULT
		)
	VALUES (
		@SLSMOD_ID,
		@SLSMOD_DEFAULT
		)

	SELECT @SLSMOD_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


