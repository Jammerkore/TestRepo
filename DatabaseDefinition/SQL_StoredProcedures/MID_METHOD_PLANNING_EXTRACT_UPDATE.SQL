--dv Description:	Updates a row into the method planning extract table
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_PLANNING_EXTRACT_UPDATE]
	@METHOD_RID int,
	@HN_RID int, 
	@FV_RID int, 
	@CDR_RID int, 
	@STORE_FILTER_RID int, 
	@CHAIN_IND char(1),
	@STORE_IND char(1),
	@ATTRIBUTE_SET_IND char(1),
	@ATTRIBUTE_RID int,
	@LOW_LEVELS_IND char(1), 
	@LOW_LEVELS_ONLY_IND char(1),
	@LOW_LEVELS_TYPE int, 
	@LOW_LEVEL_SEQUENCE int, 
	@LOW_LEVEL_OFFSET int, 
	@SHOW_INELIGIBLE_IND char(1), 
	@EXCLUDE_ZERO_VALUES_IND char(1), 
	@CONCURRENT_PROCESSES int,
	@OLL_RID int,
	@UPDATE_DATE datetime,
        @EXTRACT_DATE datetime
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE METHOD_PLANNING_EXTRACT 
	SET
		HN_RID = @HN_RID,
		FV_RID = @FV_RID,
		CDR_RID = @CDR_RID,
		STORE_FILTER_RID = @STORE_FILTER_RID,
		CHAIN_IND = @CHAIN_IND,
	    STORE_IND = @STORE_IND,
	    ATTRIBUTE_SET_IND = @ATTRIBUTE_SET_IND,
	    ATTRIBUTE_RID = @ATTRIBUTE_RID,
		LOW_LEVELS_IND = @LOW_LEVELS_IND,
		LOW_LEVELS_ONLY_IND = @LOW_LEVELS_ONLY_IND,
		LOW_LEVELS_TYPE = @LOW_LEVELS_TYPE,
		LOW_LEVEL_SEQUENCE = @LOW_LEVEL_SEQUENCE,
		LOW_LEVEL_OFFSET = @LOW_LEVEL_OFFSET,
		SHOW_INELIGIBLE_IND = @SHOW_INELIGIBLE_IND,
		EXCLUDE_ZERO_VALUES_IND = @EXCLUDE_ZERO_VALUES_IND,
		CONCURRENT_PROCESSES = @CONCURRENT_PROCESSES,
		OLL_RID = @OLL_RID,
		UPDATE_DATE = @UPDATE_DATE,
		EXTRACT_DATE = @EXTRACT_DATE 
	WHERE METHOD_RID = @METHOD_RID

	SELECT @@ROWCOUNT

END
GO