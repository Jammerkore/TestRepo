CREATE PROCEDURE [dbo].[SP_MID_USER_SESSION_INSERT] 
	(
	@USER_RID INT,
	@SESSION_STATUS INT,
	@LOGIN_DATETIME DATETIME,
	@COMPUTER_NAME VARCHAR(50),
	@SESSION_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO USER_SESSION (
		USER_RID,
		SESSION_STATUS,
		LOGIN_DATETIME,
		COMPUTER_NAME
		)
	VALUES (
		@USER_RID,
		@SESSION_STATUS,
		@LOGIN_DATETIME,
		@COMPUTER_NAME
		)

	SELECT @SESSION_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


