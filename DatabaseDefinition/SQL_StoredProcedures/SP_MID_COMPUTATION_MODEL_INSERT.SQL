CREATE PROCEDURE [dbo].[SP_MID_COMPUTATION_MODEL_INSERT] 
	(
	@MODEL_ID VARCHAR(50),
	@CALC_MODE VARCHAR(50),
	@MODEL_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO COMPUTATION_MODEL (
		COMP_MODEL_ID,
		CALC_MODE
		)
	VALUES (
		@MODEL_ID,
		@CALC_MODE
		)

	SELECT @MODEL_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


