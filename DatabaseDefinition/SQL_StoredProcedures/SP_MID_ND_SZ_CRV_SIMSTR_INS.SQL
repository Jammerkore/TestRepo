CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_SIMSTR_INS] 
	(
	@HN_RID INT,
	@ST_RID INT,
	@SS_RID INT = NULL,
	@UNTIL_DATE INT = NULL
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	INSERT INTO NODE_SIZE_CURVE_SIMILAR_STORE (
		HN_RID,
		ST_RID,
		SS_RID,
		UNTIL_DATE
		)
	VALUES (
		@HN_RID,
		@ST_RID,
		@SS_RID,
		@UNTIL_DATE
		)
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


