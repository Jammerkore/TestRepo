CREATE PROCEDURE [dbo].[SP_MID_COLOR_INSERT] 
	@COLOR_CODE_ID VARCHAR(50),
	@COLOR_CODE_NAME VARCHAR(250),
	@COLOR_CODE_GROUP VARCHAR(250),
	@VIRTUAL_IND CHAR,
	@PURPOSE INT,
	@COLOR_CODE_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO COLOR_CODE (
		COLOR_CODE_ID,
		COLOR_CODE_NAME,
		COLOR_CODE_GROUP,
		VIRTUAL_IND,
		PURPOSE
		)
	VALUES (
		@COLOR_CODE_ID,
		@COLOR_CODE_NAME,
		@COLOR_CODE_GROUP,
		@VIRTUAL_IND,
		@PURPOSE
		)

	SELECT @COLOR_CODE_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


