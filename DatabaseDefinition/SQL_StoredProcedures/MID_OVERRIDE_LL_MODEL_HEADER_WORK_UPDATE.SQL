CREATE PROCEDURE [dbo].[MID_OVERRIDE_LL_MODEL_HEADER_WORK_UPDATE]
	@NAME varchar(250),
	@OLL_RID int,
	@HN_RID int,
	@HIGH_LEVEL_HN_RID int,
	@HIGH_LEVEL_SEQ int,
	@HIGH_LEVEL_OFFEST int,
	@HIGH_LEVEL_TYPE int,
	@LOW_LEVEL_SEQ int,
	@LOW_LEVEL_OFFEST int,
	@LOW_LEVEL_TYPE int,
	@USER_RID int,
	@ACTIVE_ONLY_IND char(1)

AS
BEGIN
	SET NOCOUNT ON;

	UPDATE OVERRIDE_LL_MODEL_HEADER_WORK 
	SET 
		NAME = @NAME,
		OLL_RID = @OLL_RID, 
		HN_RID = @HN_RID,
		HIGH_LEVEL_HN_RID = @HIGH_LEVEL_HN_RID,
		HIGH_LEVEL_SEQ = @HIGH_LEVEL_SEQ,
		HIGH_LEVEL_OFFSET = @HIGH_LEVEL_OFFEST,
		HIGH_LEVEL_TYPE = @HIGH_LEVEL_TYPE,
		LOW_LEVEL_OFFSET = @LOW_LEVEL_OFFEST,
		LOW_LEVEL_SEQ = @LOW_LEVEL_SEQ,
		@LOW_LEVEL_TYPE = @LOW_LEVEL_TYPE,
		USER_RID = @USER_RID,
		ACTIVE_ONLY_IND = @ACTIVE_ONLY_IND
	WHERE OLL_RID = @OLL_RID

	SELECT @@ROWCOUNT;
END
GO