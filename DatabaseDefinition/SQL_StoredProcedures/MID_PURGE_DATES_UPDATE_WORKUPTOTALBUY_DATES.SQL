CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_WORKUPTOTALBUY_DATES]
	@PURGE_HEADERS_WORKUPTOTALBUY_DATETIME datetime,
	@PURGE_HEADERS_WORKUPTOTALBUY INT,
	@PURGE_HEADERS_WEEKS_WORKUPTOTALBUY INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_WORKUPTOTALBUY_DATETIME = @PURGE_HEADERS_WORKUPTOTALBUY_DATETIME,
		PURGE_HEADERS_WORKUPTOTALBUY = @PURGE_HEADERS_WORKUPTOTALBUY
	WHERE PURGE_HEADERS_WEEKS_WORKUPTOTALBUY = @PURGE_HEADERS_WEEKS_WORKUPTOTALBUY

	SELECT @@ROWCOUNT
END
GO




