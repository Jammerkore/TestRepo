CREATE PROCEDURE [dbo].[MID_WORKFLOW_UPDATE] 
	@WORKFLOW_RID INT,
	@WORKFLOW_NAME VARCHAR(50),
	@WORKFLOW_TYPE_ID INT,
	@WORKFLOW_USER_RID INT,
	@WORKFLOW_DESCRIPTION VARCHAR(250),
	@STORE_FILTER_RID INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE WORKFLOW
	SET 
		WORKFLOW_NAME = @WORKFLOW_NAME,
		WORKFLOW_TYPE_ID = @WORKFLOW_TYPE_ID,
		WORKFLOW_USER_RID = @WORKFLOW_USER_RID,
		WORKFLOW_DESCRIPTION = @WORKFLOW_DESCRIPTION,
		STORE_FILTER_RID = @STORE_FILTER_RID
	WHERE WORKFLOW_RID = @WORKFLOW_RID

	SELECT @@ROWCOUNT
END
GO











