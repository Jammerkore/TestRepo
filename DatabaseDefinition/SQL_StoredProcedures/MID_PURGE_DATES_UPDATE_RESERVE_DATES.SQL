--dv Description: Update purge dates for reserve weeks.
--dv =============================================
CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_RESERVE_DATES]
	@PURGE_HEADERS_RESERVE_DATETIME datetime,
	@PURGE_HEADERS_RESERVE INT,
	@PURGE_HEADERS_WEEKS_RESERVE INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_RESERVE_DATETIME = @PURGE_HEADERS_RESERVE_DATETIME,
		PURGE_HEADERS_RESERVE = @PURGE_HEADERS_RESERVE
	WHERE PURGE_HEADERS_WEEKS_RESERVE = @PURGE_HEADERS_WEEKS_RESERVE

	SELECT @@ROWCOUNT
END
GO


