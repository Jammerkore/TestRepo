--dv Description: Update purge dates for PO weeks.
--dv =============================================
CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_PO_DATES]
	@PURGE_HEADERS_PO_DATETIME datetime,
	@PURGE_HEADERS_PO INT,
	@PURGE_HEADERS_WEEKS_PO INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_PO_DATETIME = @PURGE_HEADERS_PO_DATETIME,
		PURGE_HEADERS_PO = @PURGE_HEADERS_PO
	WHERE PURGE_HEADERS_WEEKS_PO = @PURGE_HEADERS_WEEKS_PO

	SELECT @@ROWCOUNT
END
GO



