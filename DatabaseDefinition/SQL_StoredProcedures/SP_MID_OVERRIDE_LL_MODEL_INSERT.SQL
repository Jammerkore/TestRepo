CREATE PROCEDURE [dbo].[SP_MID_OVERRIDE_LL_MODEL_INSERT] 
	@NAME VARCHAR(250),
	@HN_RID INT,
	@HIGH_LEVEL_HN_RID INT,
	@HIGH_LEVEL_SEQ INT,
	@HIGH_LEVEL_OFFSET INT,
	@HIGH_LEVEL_TYPE INT,
	@LOW_LEVEL_SEQ INT,
	@LOW_LEVEL_OFFSET INT,
	@LOW_LEVEL_TYPE INT,
	@ACTIVE_ONLY_IND CHAR,
	@OLL_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO OVERRIDE_LL_MODEL_HEADER (
		NAME,
		HN_RID,
		HIGH_LEVEL_HN_RID,
		HIGH_LEVEL_SEQ,
		HIGH_LEVEL_OFFSET,
		HIGH_LEVEL_TYPE,
		LOW_LEVEL_SEQ,
		LOW_LEVEL_OFFSET,
		LOW_LEVEL_TYPE,
		ACTIVE_ONLY_IND
		)
	VALUES (
		@NAME,
		@HN_RID,
		@HIGH_LEVEL_HN_RID,
		@HIGH_LEVEL_SEQ,
		@HIGH_LEVEL_OFFSET,
		@HIGH_LEVEL_TYPE,
		@LOW_LEVEL_SEQ,
		@LOW_LEVEL_OFFSET,
		@LOW_LEVEL_TYPE,
		@ACTIVE_ONLY_IND
		)

	SELECT @OLL_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


