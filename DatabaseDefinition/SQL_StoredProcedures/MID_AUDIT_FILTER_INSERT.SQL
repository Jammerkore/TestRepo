--dv Description:	Inserts a row into the audit filter table
--dv =============================================
CREATE PROCEDURE [dbo].[MID_AUDIT_FILTER_INSERT] 
	@USER_RID int,
	@RUN_DATE_TYPE int,
	@RUN_DATE_BETWEEN_FROM int, 
	@RUN_DATE_BETWEEN_TO int, 
	@RUN_DATE_FROM datetime, 
	@RUN_DATE_TO datetime,
	@PROCESS_HIGHEST_MESSAGE_LEVEL int,
	@DETAIL_HIGHEST_MESSAGE_LEVEL int,
	@DURATION int,
	@SHOW_RUNNING char(1), 
	@SHOW_COMPLETED char(1), 
	@SHOW_MY_TASKS_ONLY char(1)
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO AUDIT_FILTER
		(
		USER_RID, 
		RUN_DATE_TYPE,
		RUN_DATE_BETWEEN_FROM, 
		RUN_DATE_BETWEEN_TO, 
		RUN_DATE_FROM, 
		RUN_DATE_TO,
		PROCESS_HIGHEST_MESSAGE_LEVEL,
		DETAIL_HIGHEST_MESSAGE_LEVEL,
		DURATION,
		SHOW_RUNNING, 
		SHOW_COMPLETED, 
		SHOW_MY_TASKS_ONLY
		) 
	VALUES
		(
		@USER_RID,
		@RUN_DATE_TYPE,
		@RUN_DATE_BETWEEN_FROM, 
		@RUN_DATE_BETWEEN_TO, 
		@RUN_DATE_FROM, 
		@RUN_DATE_TO,
		@PROCESS_HIGHEST_MESSAGE_LEVEL,
		@DETAIL_HIGHEST_MESSAGE_LEVEL,
		@DURATION,
		@SHOW_RUNNING, 
		@SHOW_COMPLETED, 
		@SHOW_MY_TASKS_ONLY
		)

	SELECT @@ROWCOUNT;
END
GO