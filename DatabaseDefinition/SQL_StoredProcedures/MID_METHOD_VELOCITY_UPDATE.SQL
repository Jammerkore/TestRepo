CREATE PROCEDURE [dbo].[MID_METHOD_VELOCITY_UPDATE]
	@METHOD_RID int,
	@SG_RID int,
	@OTS_PLAN_HN_RID int,
	@OTS_PLAN_PH_RID int,
	@OTS_PLAN_PHL_SEQUENCE int,
	@SIM_STORE_IND char(1),
	@AVG_USING_CHAIN_IND char(1),
	@SHIP_USING_BASIS_IND char(1),
	@TREND_PERCENT char(1),
	@OTS_BEGIN_CDR_RID int,
	@BALANCE_IND char(1),
	@APPLY_MIN_MAX_IND char(1),
	@RECONCILE_IND char(1),
	@INVENTORY_IND char(1),
	@MERCH_TYPE int,
	@MERCH_HN_RID int,
	@MERCH_PH_RID int,
	@MERCH_PHL_SEQUENCE int,
	@OTS_SHIP_TO_CDR_RID int,
	@GRADE_VARIABLE_TYPE int,
	@BALANCE_TO_HEADER_IND char(1)
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE METHOD_VELOCITY
	SET
		SG_RID = @SG_RID,
		OTS_PLAN_HN_RID = @OTS_PLAN_HN_RID,
		OTS_PLAN_PH_RID = @OTS_PLAN_PH_RID,
		OTS_PLAN_PHL_SEQUENCE = @OTS_PLAN_PHL_SEQUENCE,
		SIM_STORE_IND = @SIM_STORE_IND,
		AVG_USING_CHAIN_IND = @AVG_USING_CHAIN_IND,
		SHIP_USING_BASIS_IND = @SHIP_USING_BASIS_IND,
		TREND_PERCENT = @TREND_PERCENT,
		OTS_BEGIN_CDR_RID = @OTS_BEGIN_CDR_RID,
		BALANCE_IND = @BALANCE_IND,
		APPLY_MIN_MAX_IND = @APPLY_MIN_MAX_IND, 
		RECONCILE_IND = @RECONCILE_IND,
		INVENTORY_IND = @INVENTORY_IND,
		MERCH_TYPE = @MERCH_TYPE,
		MERCH_HN_RID = @MERCH_HN_RID,
		MERCH_PH_RID = @MERCH_PH_RID,
		MERCH_PHL_SEQUENCE = @MERCH_PHL_SEQUENCE,
		OTS_SHIP_TO_CDR_RID = @OTS_SHIP_TO_CDR_RID,
		GRADE_VARIABLE_TYPE = @GRADE_VARIABLE_TYPE,
		BALANCE_TO_HEADER_IND = @BALANCE_TO_HEADER_IND
	WHERE METHOD_RID=@METHOD_RID

	SELECT @@ROWCOUNT;
END
GO