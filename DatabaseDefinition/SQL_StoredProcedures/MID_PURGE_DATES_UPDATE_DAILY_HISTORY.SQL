CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_DAILY_HISTORY]
	@PURGE_DAILY_HISTORY INT,
	@PURGE_DAILY_HISTORY_WEEKS INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_DAILY_HISTORY = @PURGE_DAILY_HISTORY
	--WHERE PURGE_DAILY_HISTORY_WEEKS = NULL
	WHERE PURGE_DAILY_HISTORY_WEEKS = @PURGE_DAILY_HISTORY_WEEKS

	SELECT @@ROWCOUNT
END
GO
