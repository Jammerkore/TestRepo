CREATE PROCEDURE [dbo].[SP_MID_STORECHARGROUP_INSERT] 
	@SCG_ID VARCHAR(50),
	@SCG_TYPE INT,
	@SCG_LIST_IND CHAR,
	@SCG_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO STORE_CHAR_GROUP (
		SCG_ID,
		SCG_TYPE,
		SCG_LIST_IND
		)
	VALUES (
		@SCG_ID,
		@SCG_TYPE,
		@SCG_LIST_IND
		)

	SELECT @SCG_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


