CREATE PROCEDURE [dbo].[SP_MID_FOLDER_INSERT] 
	(
	@USER_ID INT,
	@FOLDER_ID VARCHAR(100),
	@FOLDER_TYPE INT,
	@FOLDER_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FOLDER (
		USER_RID,
		FOLDER_ID,
		FOLDER_TYPE
		)
	VALUES (
		@USER_ID,
		@FOLDER_ID,
		@FOLDER_TYPE
		)

	SELECT @FOLDER_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


