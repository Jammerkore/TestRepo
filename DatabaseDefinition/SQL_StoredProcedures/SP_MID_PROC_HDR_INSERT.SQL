CREATE PROCEDURE [dbo].[SP_MID_PROC_HDR_INSERT] 
	@PROCESS_ID INT,
	@COMPLETION_STATUS_CODE INT,
	@EXECUTION_STATUS_CODE INT,
	@START_TIME DATETIME,
	@USER_RID INT,
	@PROCESS_SUMMARY INT,
	@PROCESS_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO PROC_HDR (
		PROCESS_ID,
		COMPLETION_STATUS_CODE,
		EXECUTION_STATUS_CODE,
		START_TIME,
		USER_RID,
		SUMMARY_CODE,
		HIGHEST_LEVEL
		)
	VALUES (
		@PROCESS_ID,
		@COMPLETION_STATUS_CODE,
		@EXECUTION_STATUS_CODE,
		@START_TIME,
		@USER_RID,
		@PROCESS_SUMMARY,
		0
		)

	SELECT @PROCESS_RID = SCOPE_IDENTITY();

END
GO


