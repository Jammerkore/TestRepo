CREATE PROCEDURE [dbo].[SP_MID_AUDIT_FORECAST_INSERT] 
	@PROCESS_DATE_TIME DATETIME,
	@PROCESS_RID INT,
	@USER_RID INT,
	@HN_RID INT,
	@METHOD_RID INT,
	@METHOD_NAME VARCHAR(50),
	@METHOD_TYPE INT,
	@STORE_FV_RID INT,
	@CHAIN_FV_RID INT,
	@TIME_RANGE_TYPE CHAR,
	@TIME_RANGE_NAME VARCHAR(50),
	@TIME_RANGE_DISPLAY VARCHAR(50),
	@TIME_RANGE_BEGIN INT,
	@TIME_RANGE_END INT,
	@SG_RID INT,
	@ATTRIBUTE_NAME VARCHAR(50),
	@HN_TEXT VARCHAR(150), --tt#1049 - Forecast Audit and Trend Tab Apply to do not match on the merchandise levels - apicchetti - 1/12/2010
	@AUDIT_FORECAST_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO AUDIT_FORECAST (
		PROCESS_DATE_TIME,
		PROCESS_RID,
		USER_RID,
		HN_RID,
		METHOD_RID,
		METHOD_NAME,
		METHOD_TYPE,
		STORE_FV_RID,
		CHAIN_FV_RID,
		TIME_RANGE_TYPE,
		TIME_RANGE_NAME,
		TIME_RANGE_DISPLAY,
		TIME_RANGE_BEGIN,
		TIME_RANGE_END,
		SG_RID,
		ATTRIBUTE_NAME,
		HN_TEXT
		)
	VALUES (
		@PROCESS_DATE_TIME,
		@PROCESS_RID,
		@USER_RID,
		@HN_RID,
		@METHOD_RID,
		@METHOD_NAME,
		@METHOD_TYPE,
		@STORE_FV_RID,
		@CHAIN_FV_RID,
		@TIME_RANGE_TYPE,
		@TIME_RANGE_NAME,
		@TIME_RANGE_DISPLAY,
		@TIME_RANGE_BEGIN,
		@TIME_RANGE_END,
		@SG_RID,
		@ATTRIBUTE_NAME,
		@HN_TEXT
		) -- tt#1049 - Forecast Audit and Trend Tab Apply to do not match on the merchandise levels - apicchetti - 1/12/2010

	SELECT @AUDIT_FORECAST_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


