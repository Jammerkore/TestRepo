CREATE PROCEDURE [dbo].[MID_METHOD_BLD_PACKS_COMBO_PATTERN_UPDATE]
	@METHOD_RID int,
	@COMBO_RID int,
	@PATTERN_PACK_RID int,
	@PATTERN_NAME varchar(50),
	@PATTERN_PACK_MULT int,
	@COMBO_MAX_PATTERN_PACKS int
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE METHOD_BLD_PACKS_COMBO_PATTERN 
	SET 
		PATTERN_NAME = @PATTERN_NAME,
		PATTERN_PACK_MULT = @PATTERN_PACK_MULT,
		COMBO_MAX_PATTERN_PACKS = @COMBO_MAX_PATTERN_PACKS
    WHERE METHOD_RID=@METHOD_RID
          AND COMBO_RID=@COMBO_RID
		  AND PATTERN_PACK_RID=@PATTERN_PACK_RID

	SELECT @@ROWCOUNT;

END
GO