CREATE PROCEDURE [dbo].[SP_MID_COMP_CRIT_INSERT] 
	@EXPRESSION VARCHAR(250),
	@SIZE_CODE_RID INT,
	@COLOR_CODE_RID INT,
	@PACK_NAME VARCHAR(100),
	@COMP_CRIT_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO COMPONENT_CRITERIA (
		EXPRESSION,
		SIZE_CODE_RID,
		COLOR_CODE_RID,
		PACK_NAME
		)
	VALUES (
		@EXPRESSION,
		@SIZE_CODE_RID,
		@COLOR_CODE_RID,
		@PACK_NAME
		)

	SELECT @COMP_CRIT_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


