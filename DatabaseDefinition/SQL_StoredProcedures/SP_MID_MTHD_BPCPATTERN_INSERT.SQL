CREATE PROCEDURE [dbo].[SP_MID_MTHD_BPCPATTERN_INSERT] 
	@METHOD_RID INT,
	@COMBO_RID INT,
	@PATTERN_NAME VARCHAR(50),
	@PATTERN_PACK_MULT INT,
	@COMBO_MAX_PATTERN_PACKS INT,
	@PACK_PATTERN_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO METHOD_BLD_PACKS_COMBO_PATTERN (
		METHOD_RID,
		COMBO_RID,
		PATTERN_NAME,
		PATTERN_PACK_MULT,
		COMBO_MAX_PATTERN_PACKS
		)
	VALUES (
		@METHOD_RID,
		@COMBO_RID,
		@PATTERN_NAME,
		@PATTERN_PACK_MULT,
		@COMBO_MAX_PATTERN_PACKS
		)

	SELECT @PACK_PATTERN_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


