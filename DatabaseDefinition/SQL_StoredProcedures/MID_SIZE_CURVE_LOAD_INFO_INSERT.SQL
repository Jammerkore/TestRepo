--dv Description:	Inserts a row into the size curve load info table
--dv =============================================
CREATE PROCEDURE [dbo].[MID_SIZE_CURVE_LOAD_INFO_INSERT]
	@PROCESS_RID int,
	@CURVES_READ int,
	@CURVES_WITH_ERRORS int,
	@CURVES_CREATED int,
	@CURVES_MODIFIED int,
	@CURVES_REMOVED int,
	@GROUPS_READ int,
	@GROUPS_WITH_ERRORS int,
	@GROUPS_CREATED int,
	@GROUPS_MODIFIED int,
	@GROUPS_REMOVED int
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO SIZE_CURVE_LOAD_INFO
		(
		PROCESS_RID, 
		CURVES_READ,
		CURVES_WITH_ERRORS,
		CURVES_CREATED,
		CURVES_MODIFIED,
		CURVES_REMOVED,
		GROUPS_READ,
		GROUPS_WITH_ERRORS,
		GROUPS_CREATED,
		GROUPS_MODIFIED,
		GROUPS_REMOVED 
		) 
	VALUES
		(
		@PROCESS_RID, 
		@CURVES_READ,
		@CURVES_WITH_ERRORS,
		@CURVES_CREATED,
		@CURVES_MODIFIED,
		@CURVES_REMOVED,
		@GROUPS_READ,
		@GROUPS_WITH_ERRORS,
		@GROUPS_CREATED,
		@GROUPS_MODIFIED,
		@GROUPS_REMOVED 
		)

	SELECT @@ROWCOUNT;

END
GO
