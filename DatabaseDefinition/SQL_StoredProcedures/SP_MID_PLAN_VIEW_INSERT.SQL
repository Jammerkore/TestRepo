CREATE PROCEDURE [dbo].[SP_MID_PLAN_VIEW_INSERT] 
	(
	@USER_RID INT,
	@VIEW_ID VARCHAR(250),
	@GROUPBY_TYPE INT,
	@VIEW_RID INT OUTPUT
	)
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO PLAN_VIEW (
		USER_RID,
		VIEW_ID,
		GROUPBY_TYPE
		)
	VALUES (
		@USER_RID,
		@VIEW_ID,
		@GROUPBY_TYPE
		)

	SELECT @VIEW_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


