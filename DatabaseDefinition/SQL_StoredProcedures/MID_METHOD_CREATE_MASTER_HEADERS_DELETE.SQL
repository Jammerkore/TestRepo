CREATE PROCEDURE [dbo].[MID_METHOD_CREATE_MASTER_HEADERS_DELETE] 
	@METHOD_RID int
AS
BEGIN
	SET NOCOUNT ON;

	DELETE 
	FROM METHOD_CREATE_MASTER_HEADERS_MERCHANDISE WITH (ROWLOCK)
	WHERE METHOD_RID = @METHOD_RID

	DELETE 
	FROM METHOD_CREATE_MASTER_HEADERS_OVERRIDE WITH (ROWLOCK)
	WHERE METHOD_RID = @METHOD_RID

	DELETE 
	FROM METHOD_CREATE_MASTER_HEADERS WITH (ROWLOCK)
	WHERE METHOD_RID = @METHOD_RID

	SELECT @@ROWCOUNT;
	 
END
GO









