CREATE PROCEDURE [dbo].[SP_MID_STORE_UPDATE] 
	@ST_RID INT,
	@ST_ID VARCHAR(50),
	@STORE_NAME VARCHAR(50),
	@STORE_DESC VARCHAR(50),
	@ACTIVE_IND CHAR,
	@CITY VARCHAR(50),
	@STATE VARCHAR(50),
	@SELLING_SQ_FT INT,
	@SELLING_OPEN_DATE DATETIME,
	@SELLING_CLOSE_DATE DATETIME,
	@STOCK_OPEN_DATE DATETIME,
	@STOCK_CLOSE_DATE DATETIME,
	@LEAD_TIME INT,
	@SHIP_ON_MONDAY CHAR,
	@SHIP_ON_TUESDAY CHAR,
	@SHIP_ON_WEDNESDAY CHAR,
	@SHIP_ON_THURSDAY CHAR,
	@SHIP_ON_FRIDAY CHAR,
	@SHIP_ON_SATURDAY CHAR,
	@SHIP_ON_SUNDAY CHAR,
	/* BEGIN TT#1401 - stodd */
	@SIMILAR_STORE_MODEL CHAR,
	@IMO_ID VARCHAR(50),
	/* END TT#1401 - stodd */
	@STORE_DELETE_IND CHAR /* TT#739-MD - delete stores */
AS
BEGIN
	SET NOCOUNT ON

	UPDATE STORES
	SET ST_ID = @ST_ID,
		STORE_NAME = @STORE_NAME,
		STORE_DESC = @STORE_DESC,
		ACTIVE_IND = @ACTIVE_IND,
		CITY = @CITY,
		STATE = @STATE,
		SELLING_SQ_FT = @SELLING_SQ_FT,
		SELLING_OPEN_DATE = @SELLING_OPEN_DATE,
		SELLING_CLOSE_DATE = @SELLING_CLOSE_DATE,
		STOCK_OPEN_DATE = @STOCK_OPEN_DATE,
		STOCK_CLOSE_DATE = @STOCK_CLOSE_DATE,
		LEAD_TIME = @LEAD_TIME,
		SHIP_ON_MONDAY = @SHIP_ON_MONDAY,
		SHIP_ON_TUESDAY = @SHIP_ON_TUESDAY,
		SHIP_ON_WEDNESDAY = @SHIP_ON_WEDNESDAY,
		SHIP_ON_THURSDAY = @SHIP_ON_THURSDAY,
		SHIP_ON_FRIDAY = @SHIP_ON_FRIDAY,
		SHIP_ON_SATURDAY = @SHIP_ON_SATURDAY,
		SHIP_ON_SUNDAY = @SHIP_ON_SUNDAY,
		/* BEGIN TT#1401 - stodd */
		SIMILAR_STORE_MODEL = @SIMILAR_STORE_MODEL,
		IMO_ID = @IMO_ID,
		/* END TT#1401 - stodd */
		STORE_DELETE_IND = @STORE_DELETE_IND /* TT#739-MD - delete stores -  */
	WHERE ST_RID = @ST_RID
	SELECT @@ROWCOUNT;
END
GO


