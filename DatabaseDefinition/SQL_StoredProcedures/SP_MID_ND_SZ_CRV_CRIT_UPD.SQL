CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_CRIT_UPD] 
	(
	@HN_RID INT,
	@NSCCD_RID INT,
	@PH_OFFSET_IND CHAR(1) = NULL,
	@PH_RID INT = NULL,
	@PHL_SEQUENCE INT = NULL,
	@PHL_OFFSET INT = NULL,
	@CDR_RID INT = NULL,
	@APPLY_LOST_SALES_IND CHAR(1) = NULL,
	@OLL_RID INT = NULL,
	@CUSTOM_OLL_RID INT = NULL,
	--Begin TT#1076 - JScott - Size Curves by Set
	--	 @SIZE_GROUP_RID int = null, @CURVE_NAME varchar(50) = null)
	@SIZE_GROUP_RID INT = NULL,
	@CURVE_NAME VARCHAR(50) = NULL,
	@SG_RID INT = NULL
	)
	--End TT#1076 - JScott - Size Curves by Set
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	DECLARE @detKey INT

	UPDATE NODE_SIZE_CURVE_CRITERIA_DETAIL
	WITH (ROWLOCK)

	SET PH_OFFSET_IND = @PH_OFFSET_IND,
		PH_RID = @PH_RID,
		PHL_SEQUENCE = @PHL_SEQUENCE,
		PHL_OFFSET = @PHL_OFFSET,
		CDR_RID = @CDR_RID,
		APPLY_LOST_SALES_IND = @APPLY_LOST_SALES_IND,
		OLL_RID = @OLL_RID,
		CUSTOM_OLL_RID = @CUSTOM_OLL_RID,
		SIZE_GROUP_RID = @SIZE_GROUP_RID,
		--Begin TT#1076 - JScott - Size Curves by Set
		--			CURVE_NAME = @CURVE_NAME
		CURVE_NAME = @CURVE_NAME,
		SG_RID = @SG_RID
	--End TT#1076 - JScott - Size Curves by Set
	WHERE NSCCD_RID = @NSCCD_RID
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
	
END
GO


