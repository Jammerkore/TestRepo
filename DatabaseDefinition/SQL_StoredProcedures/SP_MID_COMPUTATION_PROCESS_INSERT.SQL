CREATE PROCEDURE [dbo].[SP_MID_COMPUTATION_PROCESS_INSERT] 
	@PROCESS_RID INT,
	@COMPUTATION_TYPE INT,
	@STATUS_CODE INT,
	@START_TIME DATETIME,
	@COMPUTATION_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO COMPUTATION_PROCESS (
		PROCESS_RID,
		COMPUTATION_TYPE,
		STATUS_CODE,
		START_TIME
		)
	VALUES (
		@PROCESS_RID,
		@COMPUTATION_TYPE,
		@STATUS_CODE,
		@START_TIME
		)

	SELECT @COMPUTATION_RID = SCOPE_IDENTITY()
END
GO


