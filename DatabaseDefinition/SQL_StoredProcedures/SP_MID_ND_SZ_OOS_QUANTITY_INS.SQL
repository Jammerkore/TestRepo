CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_OOS_QUANTITY_INS] 
	(
	@HN_RID INT,
	@SGL_RID INT,
	@COLOR_CODE_RID INT,
	@SIZES_RID INT,
	@DIMENSIONS_RID INT,
	@ROW_TYPE_ID INT,
	@SIZE_CODE_RID INT,
	@OOS_QUANTITY INT = NULL
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	BEGIN
		INSERT INTO NODE_SIZE_OUTOFSTOCK_QUANTITY (
			HN_RID,
			SGL_RID,
			COLOR_CODE_RID,
			SIZES_RID,
			DIMENSIONS_RID,
			ROW_TYPE_ID,
			SIZE_CODE_RID,
			OOS_QUANTITY
			)
		VALUES (
			@HN_RID,
			@SGL_RID,
			@COLOR_CODE_RID,
			@SIZES_RID,
			@DIMENSIONS_RID,
			@ROW_TYPE_ID,
			@SIZE_CODE_RID,
			@OOS_QUANTITY
			)
	END
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


