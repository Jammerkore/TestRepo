--dv =============================================
--dv Create date:	6/25/2014
--dv Description:	Inserts a filter condition and returns the new condition RID
--dv History:		9/29/2014 TT#1313-MD -jsobek -Header Filters
--dv =============================================
CREATE PROCEDURE [dbo].[MID_FILTER_CONDITION_INSERT]
	@FILTER_RID int,
    @SEQ int,
    @PARENT_SEQ int,
    @SIBLING_SEQ int,
    @ELEMENT_GROUP_TYPE_INDEX int,
    @LOGIC_INDEX int,
    @FIELD_INDEX int,
    @OPERATOR_INDEX int,
    @VALUE_TYPE_INDEX int,
    @DATE_TYPE_INDEX int,
    @NUMERIC_TYPE_INDEX int,
    @VALUE_TO_COMPARE varchar(200),
    @VALUE_TO_COMPARE_DOUBLE float,
    @VALUE_TO_COMPARE_DOUBLE2 float,
    @VALUE_TO_COMPARE_INT int,
    @VALUE_TO_COMPARE_INT2 int,
    @VALUE_TO_COMPARE_BOOL bit,
    @VALUE_TO_COMPARE_DATE_FROM datetime,
    @VALUE_TO_COMPARE_DATE_TO datetime,
    @VALUE_TO_COMPARE_DATE_DAYS_FROM int,
    @VALUE_TO_COMPARE_DATE_DAYS_TO int,
    @VAR1_VARIABLE_INDEX int,
    @VAR1_VERSION_INDEX int,
    @VAR1_HN_RID int,
    @VAR1_CDR_RID int,
    @VAR1_VALUE_TYPE_INDEX int,
    @VAR1_TIME_INDEX int,
	@VAR1_IS_TIME_TOTAL bit,
    @VAR_PERCENTAGE_OPERATOR_INDEX int,
    @VAR2_VARIABLE_INDEX int,
    @VAR2_VERSION_INDEX int,
    @VAR2_HN_RID int,
    @VAR2_CDR_RID int,
    @VAR2_VALUE_TYPE_INDEX int,
    @VAR2_TIME_INDEX int,
	@VAR2_IS_TIME_TOTAL bit,
    @HEADER_HN_RID int,
    @SORT_BY_TYPE_INDEX int,
    @SORT_BY_FIELD_INDEX int,
    @LIST_VALUE_CONSTANT_INDEX int,
	@CONDITION_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO FILTER_CONDITION
	(
		FILTER_RID,
        SEQ,
        PARENT_SEQ,
        SIBLING_SEQ,
        ELEMENT_GROUP_TYPE_INDEX,
        LOGIC_INDEX,
        FIELD_INDEX,
        OPERATOR_INDEX,
        VALUE_TYPE_INDEX,
        DATE_TYPE_INDEX,
        NUMERIC_TYPE_INDEX,
        VALUE_TO_COMPARE,
        VALUE_TO_COMPARE_DOUBLE,
        VALUE_TO_COMPARE_DOUBLE2,
        VALUE_TO_COMPARE_INT,
        VALUE_TO_COMPARE_INT2,
        VALUE_TO_COMPARE_BOOL,
        VALUE_TO_COMPARE_DATE_FROM,
        VALUE_TO_COMPARE_DATE_TO,
        VALUE_TO_COMPARE_DATE_DAYS_FROM,
        VALUE_TO_COMPARE_DATE_DAYS_TO,
        VAR1_VARIABLE_INDEX,
        VAR1_VERSION_INDEX,
        VAR1_HN_RID,
        VAR1_CDR_RID,
        VAR1_VALUE_TYPE_INDEX,
        VAR1_TIME_INDEX,
		VAR1_IS_TIME_TOTAL,
        VAR_PERCENTAGE_OPERATOR_INDEX,
        VAR2_VARIABLE_INDEX,
        VAR2_VERSION_INDEX,
        VAR2_HN_RID,
        VAR2_CDR_RID,
        VAR2_VALUE_TYPE_INDEX,
        VAR2_TIME_INDEX,
		VAR2_IS_TIME_TOTAL,
        HEADER_HN_RID,
        SORT_BY_TYPE_INDEX,
        SORT_BY_FIELD_INDEX,
        LIST_VALUE_CONSTANT_INDEX
	) 
	VALUES
	(
		@FILTER_RID,
        @SEQ,
        @PARENT_SEQ,
        @SIBLING_SEQ,
        @ELEMENT_GROUP_TYPE_INDEX,
        @LOGIC_INDEX,
        @FIELD_INDEX,
        @OPERATOR_INDEX,
        @VALUE_TYPE_INDEX,
        @DATE_TYPE_INDEX,
        @NUMERIC_TYPE_INDEX,
        @VALUE_TO_COMPARE,
        @VALUE_TO_COMPARE_DOUBLE,
        @VALUE_TO_COMPARE_DOUBLE2,
        @VALUE_TO_COMPARE_INT,
        @VALUE_TO_COMPARE_INT2,
        @VALUE_TO_COMPARE_BOOL,
        @VALUE_TO_COMPARE_DATE_FROM,
        @VALUE_TO_COMPARE_DATE_TO,
        @VALUE_TO_COMPARE_DATE_DAYS_FROM,
        @VALUE_TO_COMPARE_DATE_DAYS_TO,
        @VAR1_VARIABLE_INDEX,
        @VAR1_VERSION_INDEX,
        @VAR1_HN_RID,
        @VAR1_CDR_RID,
        @VAR1_VALUE_TYPE_INDEX,
        @VAR1_TIME_INDEX,
		@VAR1_IS_TIME_TOTAL,
        @VAR_PERCENTAGE_OPERATOR_INDEX,
        @VAR2_VARIABLE_INDEX,
        @VAR2_VERSION_INDEX,
        @VAR2_HN_RID,
        @VAR2_CDR_RID,
        @VAR2_VALUE_TYPE_INDEX,
        @VAR2_TIME_INDEX,
		@VAR2_IS_TIME_TOTAL,
        @HEADER_HN_RID,
        @SORT_BY_TYPE_INDEX,
        @SORT_BY_FIELD_INDEX,
        @LIST_VALUE_CONSTANT_INDEX
	)

	SELECT @CONDITION_RID = SCOPE_IDENTITY()

END
GO
