CREATE PROCEDURE [dbo].[SP_MID_FOR_BAL_MODEL_INSERT] 
	@FBMOD_ID VARCHAR(50),
	@MATRIX_TYPE INT, /* BEGIN MID Track #5647 - KJohnson - Matrix Forecast */
	@ITERATIONS_TYPE INT,
	@ITERATIONS_COUNT INT,
	@BALANCE_MODE INT,
	@CALC_MODE VARCHAR(50),
	@FBMOD_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FORECAST_BAL_MODEL (
		FBMOD_ID,
		MATRIX_TYPE,
		ITERATIONS_TYPE,
		ITERATIONS_COUNT,
		BALANCE_MODE,
		CALC_MODE
		)
	VALUES (
		@FBMOD_ID,
		@MATRIX_TYPE,
		@ITERATIONS_TYPE,
		@ITERATIONS_COUNT,
		@BALANCE_MODE,
		@CALC_MODE
		)

	SELECT @FBMOD_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


