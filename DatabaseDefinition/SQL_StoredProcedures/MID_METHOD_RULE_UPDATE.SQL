CREATE PROCEDURE [dbo].[MID_METHOD_RULE_UPDATE]
	@METHOD_RID int,
	@STORE_FILTER_RID int, 
	@HDR_RID int, 
	@STORE_ORDER int,
    @HEADER_COMPONENT int, 
	@HDR_PACK_RID int, 
	@HDR_BC_RID int, 
	@INCLUDED_STORES int, 
	@INCLUDED_QUANTITY int,
	@EXCLUDED_STORES int, 
	@EXCLUDED_QUANTITY int, 
	@SGL_RID int, 
	@IS_HEADER_MASTER char,
	@INCLUDE_RESERVE_IND char
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE METHOD_RULE
	SET
		STORE_FILTER_RID=@STORE_FILTER_RID,
		HDR_RID = @HDR_RID,
		STORE_ORDER = @STORE_ORDER,
		HEADER_COMPONENT = @HEADER_COMPONENT,
		HDR_PACK_RID = @HDR_PACK_RID,
		HDR_BC_RID = @HDR_BC_RID,
		INCLUDED_STORES = @INCLUDED_STORES,
		INCLUDED_QUANTITY = @INCLUDED_QUANTITY,
		EXCLUDED_STORES = @EXCLUDED_STORES,
		EXCLUDED_QUANTITY = @EXCLUDED_QUANTITY,
		SGL_RID = @SGL_RID,
		IS_HEADER_MASTER = @IS_HEADER_MASTER,
		INCLUDE_RESERVE_IND = @INCLUDE_RESERVE_IND
	WHERE METHOD_RID = @METHOD_RID

	SELECT @@ROWCOUNT;

END
GO
