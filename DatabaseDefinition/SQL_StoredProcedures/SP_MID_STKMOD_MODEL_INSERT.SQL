CREATE PROCEDURE [dbo].[SP_MID_STKMOD_MODEL_INSERT] 
	@STKMOD_ID VARCHAR(50),
	@STKMOD_DEFAULT FLOAT,
	@STKMOD_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO STOCK_MODIFIER_MODEL (
		STKMOD_ID,
		STKMOD_DEFAULT
		)
	VALUES (
		@STKMOD_ID,
		@STKMOD_DEFAULT
		)

	SELECT @STKMOD_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


