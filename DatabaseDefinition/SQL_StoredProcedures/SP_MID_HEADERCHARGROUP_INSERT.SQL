CREATE PROCEDURE [dbo].[SP_MID_HEADERCHARGROUP_INSERT] 
	@HCG_ID VARCHAR(50),
	@HCG_TYPE INT,
	@HCG_LIST_IND CHAR,
	@HCG_PROTECT_IND CHAR,
	@HCG_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HEADER_CHAR_GROUP (
		HCG_ID,
		HCG_TYPE,
		HCG_LIST_IND,
		HCG_PROTECT_IND
		)
	VALUES (
		@HCG_ID,
		@HCG_TYPE,
		@HCG_LIST_IND,
		@HCG_PROTECT_IND
		)

	SELECT @HCG_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


