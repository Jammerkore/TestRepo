CREATE PROCEDURE [dbo].[MID_SIZE_CODE_READ_ALL]
AS
BEGIN 
	SET NOCOUNT ON;

 	SELECT 
		SIZE_CODE_RID SIZE_CODE_RID,
		COALESCE(SIZE_CODE_ID,' ') SIZE_CODE_ID,
		COALESCE(SIZE_CODE_PRIMARY,' ') SIZE_CODE_PRIMARY,
		CASE WHEN SIZE_CODE_SECONDARY IS NULL THEN 'None' ELSE SIZE_CODE_SECONDARY END AS SIZE_CODE_SECONDARY,
		COALESCE(SIZE_CODE_PRODUCT_CATEGORY,' ') SIZE_CODE_PRODUCT_CATEGORY
    FROM SIZE_CODE
 
END
GO