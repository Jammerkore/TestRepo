CREATE PROCEDURE [dbo].[MID_METHOD_OVERRIDE_READ]
	@METHOD_RID int

AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		STORE_FILTER_RID, 
		STORE_GRADE_TIMEFRAME, 
		EXCEED_MAX_IND,
		PERCENT_NEED_LIMIT, 
		RESERVE, 
		PERCENT_IND,
		MERCH_HN_RID, 
		MERCH_PH_RID, 
		MERCH_PHL_SEQUENCE,
		ON_HAND_HN_RID, 
		ON_HAND_PH_RID, 
		ON_HAND_PHL_SEQUENCE,
		ON_HAND_FACTOR, 
		COLOR_MULT, 
		SIZE_MULT, 
		ALL_COLOR_MIN,
		ALL_COLOR_MAX, 
		SG_RID, 
		EXCEED_CAPACITY,
		MERCH_UNSPECIFIED, 
		ON_HAND_UNSPECIFIED,
		COALESCE(RESERVE_AS_BULK,0) as RESERVE_AS_BULK, 
		COALESCE(RESERVE_AS_PACKS,0) as RESERVE_AS_PACKS,
		STORE_GRADES_SG_RID,
		COALESCE(INVENTORY_IND,'A') as INVENTORY_IND,
		IB_MERCH_HN_RID, 
		IB_MERCH_PH_RID, 
		IB_MERCH_PHL_SEQUENCE, 
		IMO_SG_RID, 
		IMO_APPLY_VSW
    FROM METHOD_OVERRIDE 
	WHERE METHOD_RID = @METHOD_RID

END
GO