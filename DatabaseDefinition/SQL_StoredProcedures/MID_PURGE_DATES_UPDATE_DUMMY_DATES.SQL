CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_DUMMY_DATES]
	@PURGE_HEADERS_DUMMY_DATETIME datetime,
	@PURGE_HEADERS_DUMMY INT,
	@PURGE_HEADERS_WEEKS_DUMMY INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_DUMMY_DATETIME = @PURGE_HEADERS_DUMMY_DATETIME,
		PURGE_HEADERS_DUMMY = @PURGE_HEADERS_DUMMY
	WHERE PURGE_HEADERS_WEEKS_DUMMY = @PURGE_HEADERS_WEEKS_DUMMY

	SELECT @@ROWCOUNT
END
GO



