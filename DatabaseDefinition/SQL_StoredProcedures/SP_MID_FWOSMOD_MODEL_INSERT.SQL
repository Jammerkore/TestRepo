CREATE PROCEDURE [dbo].[SP_MID_FWOSMOD_MODEL_INSERT] 
	@FWOSMOD_ID VARCHAR(50),
	@FWOSMOD_DEFAULT FLOAT,
	@FWOSMOD_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FWOS_MODIFIER_MODEL (
		FWOSMOD_ID,
		FWOSMOD_DEFAULT
		)
	VALUES (
		@FWOSMOD_ID,
		@FWOSMOD_DEFAULT
		)

	SELECT @FWOSMOD_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


