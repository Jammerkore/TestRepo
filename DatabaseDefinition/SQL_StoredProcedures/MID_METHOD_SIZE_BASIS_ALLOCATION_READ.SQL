--dv =============================================
--dv Create date:	12/02/2013
--dv Description:	Reads size basis allocation rows for the given method
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_SIZE_BASIS_ALLOCATION_READ]
	@METHOD_RID int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT 
		METHOD_RID, 
		SIZE_BASIS_HDR_RID, 
		COLOR_CODE_RID,
		SIZE_BASIS_RULE,
		STORE_FILTER_RID, 
		SIZE_GROUP_RID, 
		SIZE_CURVE_GROUP_RID, 
		HEADER_COMPONENT, 
		RULE_QUANTITY, 
		SIZE_CONSTRAINT_RID,
		GENCURVE_HCG_RID, 
		GENCURVE_HN_RID,
		GENCURVE_PH_RID, 
		GENCURVE_PHL_SEQUENCE,
		COALESCE(GENCURVE_COLOR_IND, '0') GENCURVE_COLOR_IND,
		GENCURVE_MERCH_TYPE,
		USE_DEFAULT_CURVE_IND,
        APPLY_RULES_ONLY_IND,
        GENCURVE_NSCCD_RID,
		GENCONSTRAINT_HCG_RID, 
		GENCONSTRAINT_HN_RID,
		GENCONSTRAINT_PH_RID, 
		GENCONSTRAINT_PHL_SEQUENCE,
		COALESCE(GENCONSTRAINT_COLOR_IND, '0') GENCONSTRAINT_COLOR_IND,
		GENCONSTRAINT_MERCH_TYPE,
		COALESCE(INCLUDE_RESERVE_IND, '0') INCLUDE_RESERVE_IND
	FROM METHOD_SIZE_BASIS_ALLOCATION 
	WHERE METHOD_RID = @METHOD_RID

END
GO