CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_OOS_INS] 
	(
	@HN_RID INT,
	@SG_RID INT,
	@SIZE_GROUP_RID INT
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	BEGIN
		INSERT INTO NODE_SIZE_OUTOFSTOCK (
			HN_RID,
			SG_RID,
			SIZE_GROUP_RID
			)
		VALUES (
			@HN_RID,
			@SG_RID,
			@SIZE_GROUP_RID
			)
	END
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


