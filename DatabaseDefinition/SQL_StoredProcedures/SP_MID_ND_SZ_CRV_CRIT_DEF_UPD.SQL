CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_CRV_CRIT_DEF_UPD] 
	(
	@HN_RID INT,
	@NSCCD_RID INT
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	DELETE NODE_SIZE_CURVE_CRITERIA_DEFAULT
	WITH (ROWLOCK)
	WHERE HN_RID = @HN_RID

	IF @NSCCD_RID IS NOT NULL
	BEGIN
		INSERT INTO NODE_SIZE_CURVE_CRITERIA_DEFAULT (
			HN_RID,
			NSCCD_RID
			)
		VALUES (
			@HN_RID,
			@NSCCD_RID
			)
	END
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


