CREATE PROCEDURE [dbo].[SP_MID_ND_SZ_ST_INS] 
	(
	@HN_RID INT,
	@SELLTHRU_LIMIT FLOAT
	)
AS
BEGIN
	SET NOCOUNT ON
	SET ANSI_PADDING ON

	BEGIN
		INSERT INTO NODE_SIZE_SELLTHRU (
			HN_RID,
			SELLTHRU_LIMIT
			)
		VALUES (
			@HN_RID,
			@SELLTHRU_LIMIT
			)
	END
	SELECT @@ROWCOUNT;
	SET NOCOUNT OFF
	SET ANSI_PADDING OFF
END
GO


