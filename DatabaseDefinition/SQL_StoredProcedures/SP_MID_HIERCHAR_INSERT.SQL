CREATE PROCEDURE [dbo].[SP_MID_HIERCHAR_INSERT] 
	@HCG_RID INT,
	@HC_ID VARCHAR(50),
	@HC_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HIER_CHAR (
		HCG_RID,
		HC_ID
		)
	VALUES (
		@HCG_RID,
		@HC_ID
		)

	SELECT @HC_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


