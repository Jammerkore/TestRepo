CREATE PROCEDURE [dbo].[SP_MID_FORECAST_MODEL_INSERT] 
	@FORECAST_MOD_ID VARCHAR(50),
	@DEFAULT_IND VARCHAR(1),
	@CALC_MODE VARCHAR(50),
	@FORECAST_MOD_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO FORECAST_MODEL (
		FORECAST_MOD_ID,
		DEFAULT_IND,
		CALC_MODE
		)
	VALUES (
		@FORECAST_MOD_ID,
		@DEFAULT_IND,
		@CALC_MODE
		)

	SELECT @FORECAST_MOD_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


