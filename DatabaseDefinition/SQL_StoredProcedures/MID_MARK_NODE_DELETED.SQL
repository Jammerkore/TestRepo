CREATE PROCEDURE [dbo].[MID_MARK_NODE_DELETED] 
	@HN_RID INT,
	@NODE_DELETE_IND char,
	@NODE_ID VARCHAR(50)
AS
BEGIN
	SET NOCOUNT ON;
	UPDATE HIERARCHY_NODE WITH (ROWLOCK)
	   SET NODE_DELETE_IND = @NODE_DELETE_IND
	 WHERE HN_RID = @HN_RID

	UPDATE BASE_NODE WITH (ROWLOCK)
	   SET BN_ID = @NODE_ID
	 WHERE HN_RID = @HN_RID

	UPDATE HIER_NODE_JOIN WITH (ROWLOCK)
	   SET NODE_DELETE_IND = @NODE_DELETE_IND
	 WHERE HN_RID = @HN_RID
	
END
GO