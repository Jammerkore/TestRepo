CREATE PROCEDURE [dbo].[SP_MID_USER_INSERT] 
	@USER_NAME VARCHAR(256),
	@USER_PASSWORD VARCHAR(250),
	@USER_FULLNAME VARCHAR(250),
	@USER_DESCRIPTION VARCHAR(250),
	@USER_ACTIVE_IND CHAR,
	@USER_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO APPLICATION_USER (
		USER_NAME,
		USER_PASSWORD,
		USER_FULLNAME,
		USER_DESCRIPTION,
		USER_ACTIVE_IND
		)
	VALUES (
		@USER_NAME,
		@USER_PASSWORD,
		@USER_FULLNAME,
		@USER_DESCRIPTION,
		@USER_ACTIVE_IND
		)

	SELECT @USER_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


