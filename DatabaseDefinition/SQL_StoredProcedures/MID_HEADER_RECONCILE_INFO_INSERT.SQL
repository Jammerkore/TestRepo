--dv Create date:	03/27/2015
--dv Description:	Inserts a row into the Header Reconcile Info table
--dv =============================================
CREATE PROCEDURE [dbo].[MID_HEADER_RECONCILE_INFO_INSERT]
	@PROCESS_RID int,
	@HEADER_FILES_READ int,
	@HEADER_TRANS_READ int,
	@HEADER_TRANS_WRITTEN int,
	@HEADER_FILES_WRITTEN int,
	@HEADER_TRANS_DUPLICATES int,
	@HEADER_TRANS_SKIPPED int,
	@REMOVE_TRANS_WRITTEN int,
	@REMOVE_FILES_WRITTEN int

AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO HEADER_RECONCILE_INFO 
		(
		PROCESS_RID,
		HEADER_FILES_READ,
		HEADER_TRANS_READ,
		HEADER_TRANS_WRITTEN, 
		HEADER_FILES_WRITTEN, 
		HEADER_TRANS_DUPLICATES,
		HEADER_TRANS_SKIPPED,
		REMOVE_TRANS_WRITTEN, 
		REMOVE_FILES_WRITTEN

		) 
	VALUES
		(
		@PROCESS_RID,
		@HEADER_FILES_READ,
		@HEADER_TRANS_READ,
		@HEADER_TRANS_WRITTEN, 
		@HEADER_FILES_WRITTEN, 
		@HEADER_TRANS_DUPLICATES,
		@HEADER_TRANS_SKIPPED,
		@REMOVE_TRANS_WRITTEN, 
		@REMOVE_FILES_WRITTEN

		)
		
	SELECT @@ROWCOUNT;
		
END
GO
