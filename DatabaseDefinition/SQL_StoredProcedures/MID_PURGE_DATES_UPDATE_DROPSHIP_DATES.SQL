--dv Description: Update purge dates for drop ship weeks.
--dv =============================================
CREATE PROCEDURE [dbo].[MID_PURGE_DATES_UPDATE_DROPSHIP_DATES]
	@PURGE_HEADERS_DROPSHIP_DATETIME datetime,
	@PURGE_HEADERS_DROPSHIP INT,
	@PURGE_HEADERS_WEEKS_DROPSHIP INT
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE PURGE_DATES
	SET PURGE_HEADERS_DROPSHIP_DATETIME = @PURGE_HEADERS_DROPSHIP_DATETIME,
		PURGE_HEADERS_DROPSHIP = @PURGE_HEADERS_DROPSHIP
	WHERE PURGE_HEADERS_WEEKS_DROPSHIP = @PURGE_HEADERS_WEEKS_DROPSHIP

	SELECT @@ROWCOUNT
END
GO





