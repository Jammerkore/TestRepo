--dv Description:	Inserts a build pack
--dv =============================================
CREATE PROCEDURE [dbo].[MID_METHOD_BLD_PACKS_INSERT]
	@METHOD_RID int,
    @BPC_RID int,
	@PATTERN_COMP_MIN int,
	@PATTERN_SIZE_MULTIPLE int,
	@SIZE_GROUP_RID int,
	@SIZE_CURVE_GROUP_RID int,
    @RESERVE_TOTAL float,
	@RESERVE_TOTAL_PERCENT_IND char(1),
	@RESERVE_BULK float,
	@RESERVE_BULK_PERCENT_IND char(1),
	@RESERVE_PACKS float,
	@RESERVE_PACKS_PERCENT_IND char(1),
    @AVG_PACK_DEV_TOLERANCE float,
	@MAX_PACK_NEED_TOLERANCE float,
	@DEPLETE_RESERVE_SELECTED char(1),
	@INCREASE_BUY_SELECTED char(1),
	@INCREASE_BUY_PCT float,
	@REMOVE_BULK_IND char(1)
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO METHOD_BLD_PACKS 
		(
		METHOD_RID,
        BPC_RID,
		PATTERN_COMP_MIN,
		PATTERN_SIZE_MULTIPLE,
		SIZE_GROUP_RID,
		SIZE_CURVE_GROUP_RID,
        RESERVE_TOTAL,
		RESERVE_TOTAL_PERCENT_IND,
		RESERVE_BULK,
		RESERVE_BULK_PERCENT_IND,
		RESERVE_PACKS,
		RESERVE_PACKS_PERCENT_IND,
        AVG_PACK_DEV_TOLERANCE,
		MAX_PACK_NEED_TOLERANCE,
		DEPLETE_RESERVE_SELECTED,
		INCREASE_BUY_SELECTED,
		INCREASE_BUY_PCT,
		REMOVE_BULK_IND
		)
	VALUES 
		(
		@METHOD_RID,
        @BPC_RID,
		@PATTERN_COMP_MIN,
		@PATTERN_SIZE_MULTIPLE,
		@SIZE_GROUP_RID,
		@SIZE_CURVE_GROUP_RID,
        @RESERVE_TOTAL,
		@RESERVE_TOTAL_PERCENT_IND,
		@RESERVE_BULK,
		@RESERVE_BULK_PERCENT_IND,
		@RESERVE_PACKS,
		@RESERVE_PACKS_PERCENT_IND,
        @AVG_PACK_DEV_TOLERANCE,
		@MAX_PACK_NEED_TOLERANCE,
		@DEPLETE_RESERVE_SELECTED,
		@INCREASE_BUY_SELECTED,
		@INCREASE_BUY_PCT,
		@REMOVE_BULK_IND
		)

	SELECT @@ROWCOUNT;

END
GO