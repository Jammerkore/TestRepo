CREATE PROCEDURE [dbo].[SP_MID_HEADER_PACK_INSERT] 
	@HDR_RID INT,
	@HDR_PACK_NAME VARCHAR(100),
	@PACKS INT,
	@MULTIPLE INT,
	@RESERVE_PACKS INT,
	@GENERIC_IND CHAR,
	@ASSOCIATED_PACK_RID INT,
	@SEQ INT,
	@HDR_PACK_RID INT OUTPUT
AS
BEGIN
	SET NOCOUNT ON

	INSERT INTO HEADER_PACK (
		HDR_RID,
		HDR_PACK_NAME,
		PACKS,
		MULTIPLE,
		RESERVE_PACKS,
		GENERIC_IND,
		ASSOCIATED_PACK_RID,
		SEQ
		)
	VALUES (
		@HDR_RID,
		@HDR_PACK_NAME,
		@PACKS,
		@MULTIPLE,
		@GENERIC_IND,
		@GENERIC_IND,
		@ASSOCIATED_PACK_RID,
		@SEQ
		)

	SELECT @HDR_PACK_RID = SCOPE_IDENTITY() -- TT#3256 - JSmith - Replace @@IDENTITY with SCOPE_IDENTITY()
END
GO


